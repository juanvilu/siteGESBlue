  <h3>Monedero Electr&oacute;nico</h3>
    
  <p class="autor"><em>Autor: Jorge Enrique L&oacute;pez S&aacute;nchez</em></p>
    <h4>1. &iquest;Qu&eacute; es un Monedero Electr&oacute;nico?</h4>
    <p>Los monederos electr&oacute;nicos hoy en d&iacute;a est&aacute;n por todos 
      lados y para muestra basta un bot&oacute;n:</p>
  </div>
  <ul>
    <li> 
      <div class="texto">Tarjetas para el uso de telefon&iacute;a celular de prepago.</div>
    </li>
    <li> 
      <div class="texto">Tarjetas de consumo en locales de video-juegos. </div>
    </li>
    <li> 
      <div class="texto">Tarjetas prepagadas para llamadas telef&oacute;nicas 
        de larga distancia. Ofrecen tarifas m&aacute;s bajas que las de nuestra 
        l&iacute;nea telef&oacute;nica convencional.</div>
    </li>
    <li> 
      
    <div class="texto">Vales electr&oacute;nicos de despensa o de combustible. 
      Las empresas &quot;compran&quot; para sus trabajadores cierta cantidad de 
      dinero que ellos podr&aacute;n utilizar en algunas tiendas, supermercados 
      y estaciones de despacho de combustible.</div>
    </li>
    <li> 
      <div class="texto">En algunos pa&iacute;ses, es frecuente adquirir tarjetas 
        para el uso en transporte p&uacute;blico.</div>
    </li>
  </ul>

  <div class="texto">     
  <p>En cualquiera de los casos, el mecanismo es similar y consiste en el uso 
    de una tarjeta que almacena un valor monetario, que puede utilizarse como 
    forma de pago normal (en lugar de dinero en efectivo) y en otros casos el 
    costo de los servicios se ir&aacute; descontando del valor inicial prepagado 
    o precargado (como en la telefon&iacute;a celular).</p>
  <p align="center"><img src="<?php echo $url;?>ejemplo.gif" width="110" height="147" /></p>
    <p>Lo anterior tiene m&uacute;ltiples beneficios tanto para el usuario como 
      para el proveedor del servicio.</p>
  </div>

  <div class="texto"> 
    <p>Pues bien, las instituciones educativas no pueden abstraerse a este tipo 
      de innovaciones tecnol&oacute;gicas, y son muchas las que han optado por 
      implantar estos mecanismos en sus instalaciones con excelentes resultados.</p>
    <h4>2. Como se utiliza el Monedero Electr&oacute;nico en Instituciones Educativas.</h4>
    <p>El Monedero Electr&oacute;nico se constituye en una herramienta que hace 
      m&aacute;s eficiente el cobro y la prestaci&oacute;n de los servicios educativos. 
      Se hace m&aacute;s r&aacute;pido el tiempo de cobro, adem&aacute;s de que 
      se controla perfectamente toda la cobranza, y se evitan p&eacute;rdidas 
      por cobros no reportados y actos de corrupci&oacute;n de los cajeros.</p>
    <p>Los padres de familia pueden controlar de forma peri&oacute;dica los gastos 
      que tengan sus hijos, y decidir cuando es tiempo de comprar m&aacute;s saldo 
      para el monedero electr&oacute;nico. A la vez, los estudiantes no tienen 
      necesidad de manejar dinero en efectivo, evitando el riesgo de p&eacute;rdida 
      o de adquirir productos nocivos para la salud. </p>
    <p>Al interior de una instituci&oacute;n, el Monedero Electr&oacute;nico puede 
      utilizarse en el Restaurante/Cafeter&iacute;a/Comedor, Estacionamiento, 
      Biblioteca, Pago de Servicios Acad&eacute;micos, &Aacute;rea de Fotocopiado, 
      Salas de Deportes, etc.</p>
    
  <p>El uso de estos mecanismos es m&aacute;s amplio en colegios y universidades 
    de Europa y Estados Unidos, mientras que en M&eacute;xico y Latinoam&eacute;rica 
    comienza a cobrar popularidad.</p>
    <h4>3. Control Escolar GES</h4>
  <p>Control Escolar GES es el &uacute;nico software en su tipo, que ofrece estos 
    mecanismos de gesti&oacute;n de cobros. Proporcionando canales pr&aacute;cticos 
    y confiables para la operaci&oacute;n de Monedero Electr&oacute;nico para 
    los alumnos.</p>
  <p align="center"><img src="<?php echo $url;?>lector.gif" width="147" height="100" /></p>
    <p>El Monedero Electr&oacute;nico a trav&eacute;s de la utilizaci&oacute;n 
      de Control Escolar GES consiste en:</p>
  </div>
  <ul>
    <li> 
      
    <div class="texto">El uso de una Tarjeta de identificaci&oacute;n que almacena 
      el valor monetario prepagado. Las tarjetas pueden ser credenciales normales 
      que tengan c&oacute;digo de barras o especiales con banda magn&eacute;tica 
      o con chip (smart-card).</div>
    </li>
    <li> 
      <div class="texto"> Puede ser utilizada como forma de pago com&uacute;n 
        en cualquier m&oacute;dulo de cobro dentro de una instituci&oacute;n educativa 
        y que se encuentre enlazado al software Control Escolar GES. 
        <div class="texto"></div>
      </div>
    </li>
    <li> 
      <div class="texto"> Para usar la tarjeta de monedero electr&oacute;nico, 
        los pap&aacute;s o el alumno deber&aacute;n cargar dinero a la tarjeta. 
        Acudiendo a alguna caja de pago en el colegio (disponibilidad inmediata) 
        o realizando un dep&oacute;sito bancario (disponibilidad al d&iacute;a 
        siguiente).</div>
    </li>
    <li> 
      <div class="texto"> Al momento de realizar alguna compra, los alumnos deben 
        identificarse con el uso de un NIP (N&uacute;mero de Identificaci&oacute;n 
        Personal). El valor de las compras ser&aacute; deducido del saldo de la 
        tarjeta cada vez que el alumno pague con ella. 
        <div class="texto"></div>
      </div>
    </li>
    <li> 
      <div class="texto">El saldo y las transacciones del Monedero Electr&oacute;nico, 
        es manejado a trav&eacute;s del sistema y no ha trav&eacute;s de complicados 
        y costosos procedimientos bancarios.</div>
    </li>
    <li> 
      <div class="texto">El saldo se almacena en la base de datos del software 
        Control Escolar GES y no en la tarjeta, por lo cual no existe riesgo de 
        p&eacute;rdida del dinero si la tarjeta se pierde o se destruye.</div>
    </li>
  </ul>
  
