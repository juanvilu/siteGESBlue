  <h3>Grupo GES busca aumentar su canal de distribuci&oacute;n</h3>
    
  <p class="autor"><em>Autor: Jorge Enrique L&oacute;pez S&aacute;nchez</em></p>
    
  <p>A partir del &uacute;ltimo trimestre de este a&ntilde;o, Grupo GES iniciar&aacute; 
    una nueva estrategia consolidada de atracci&oacute;n de distribuidores. Esta 
    estrategia tendr&aacute; como puntos fuertes un mayor acercamiento, mejor 
    imagen de marca y nuevo material de apoyo en ventas.</p>
</div>
<div class="texto"> 
  <p>Durante los a&ntilde;os 2005 y 2006 se ha tenido un crecimiento importante 
    en el canal, sin embargo Grupo GES fortalecer&aacute; su estrategia de ventas 
    con diversos eventos que impactar&aacute;n en clientes prospecto y en los 
    clientes cautivos, con beneficio indirecto a los distribuidores.</p>
  <p>La propuesta de negocio consiste en capacitar integralmente en los aspectos 
    comerciales y t&eacute;cnicos, para que tengan la capacidad de ofertar un 
    buen paquete de servicios. Esta capacitaci&oacute;n consiste en:</p>
</div>

<ul>
  <li>Aspectos Comerciales a la fuerza de ventas, tales como manejo de prospecci&oacute;n, 
    cotizaciones, forma de licenciamiento, etc.</li>
  <li> Aspectos T&eacute;cnicos a Asesores y encargados de soporte al cliente, 
    quienes dar&aacute;n la cara al cliente durante la fase de implantaci&oacute;n 
    y puesta en marcha.</li>
</ul>
<p>Grupo GES buscar&aacute; de manera especial, incorporar distribuidores en las 
  Ciudades de M&eacute;xico, Guadalajara y Monterrey, en donde el potencial de 
  mercado es muy grande y a&uacute;n no se cuenta con una presencia importante.</p>
