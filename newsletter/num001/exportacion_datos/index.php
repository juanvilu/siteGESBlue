  <h3>Exportación de Datos</h3>
    
  <p class="autor"><em>Autor: Maximino Celis Carbajal</em></p>
  <p>Grupo GES esta conciente del alto valor y la gran necesidad que cada institución 
    tiene de manipular la información existente en su base de datos. Estas necesidades 
    varían enormemente entre cada institución tanto en su complejidad como en 
    su utilización. <br />
    <br />
    Por estas razones el usuario cuenta con una herramienta para poder exportar 
    cualquier información que necesite y posteriormente manipularla en total libertad.</p>
  </div>
 
<div class="texto"> 

    <h4>1. Como utilizar la Exportación de Datos.</h4>
    <p>La Exportación de Datos es la última opción del menú REPORTES.</p>
	
    <p align="center"><img src="<?php echo $url;?>reportes.gif" width="708" height="46" /></p>
    <p align="center"><img src="<?php echo $url;?>exportacion.gif" width="418" height="108" /></p>
	
    
  <p>Una vez que el usuario acceda a esta opción, el sistema desplegará una ventana 
    donde se deberá elegir alguna de las opciones predefinidas para exportar. 
    Estas opciones son:</p>

    <p align="center"><img src="<?php echo $url;?>pantalla_expor.gif"/></p>

    
  <p>Despu&eacute;s de elegir alguna de las anteriores, basta hacer &quot;clic&quot; 
    en [Exportar Informaci&oacute;n] y el sistema copiar&aacute; la informaci&oacute;n 
    hacia un archivo de Excel y la mostrará tal cual está en la Base de Datos.</p>
  
    <p align="center"><img src="<?php echo $url;?>excel.gif" /></p> 
 
    
  <h4>2. Consulta Libre</h4>
  <p>En algunas ocasiones, la informaci&oacute;n que se necesita no puede ser 
    obtenida a trav&eacute;s de las opciones predefinidas para lo cual resulta 
    necesario utilizar una <em>Consulta Libre</em>.</p>
  <p>Al seleccionar la opción [Consulta Libre], aparecerá una ventana en la cual 
    podemos ingresar manualmente una instrucción SQL para realizar libremente 
    la consulta que desee y de esta manera obtener cualquier información.</p>

  <p align="center"><img src="<?php echo $url;?>expor_libre.gif" width="609" height="366" /></p>

    <p>En cuanto obtenga el resultado, para exportarlo, solo deberá hacer clic en el botón SALIR DE ESTA OPCION, y en la pantalla anterior, hacer clic en el botón EXPORTAR INFORMACION.</p>
