<h3>Grupo GES certificar&aacute; sus procesos bajo el modelo MoProSoft.</h3>
<p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
<p align="justify"><img src="<?php echo $url;?>banner_moprosoft.gif" class="noborder"></p>
<p align="justify">En Grupo GES buscamos siempre mantener pr&aacute;cticas y modelos 
  de negocio que presenten mejores beneficios para nuestra base de clientes, por 
  lo cual a partir del mes de Mayo hemos iniciado una serie de actividades en 
  busca de obtener la certificaci&oacute;n <strong>MoProSoft</strong>.</p>
<p align="justify">Esta certificaci&oacute;n surge por iniciativa de la Secretar&iacute;a 
  de Econom&iacute;a y gracias al trabajo de acad&eacute;micos y empresarios mexicanos. 
  <strong>MoProSoft</strong> es un modelo de procesos para la industria de software 
  mexicana, que incorpora las mejores pr&aacute;cticas internacionales en ingenier&iacute;a 
  de software (9000, ISO 15504, SW-CMM y CMM-I). </p>
<p align="justify">La adopci&oacute;n de este modelo nos permitir&aacute; ofrecer 
  servicios con mejor calidad y alcanzar mejores niveles de competitividad en 
  el &aacute;mbito internacional. Adem&aacute;s, daremos cumplimiento a la norma 
  oficial NMX-059-NYCE-2005 publicada por el gobierno mexicano el 15 de agosto 
  de 2005 bajo el nombre de TECNOLOG&Iacute;A DE LA INFORMACI&Oacute;N-SOFTWARE-MODELOS 
  DE PROCESOS Y EVALUACI&Oacute;N PARA DESARROLLO Y MANTENIMIENTO DE SOFTWARE.</p>
<p>Lo anterior refuerza nuestro compromiso de proporcionar herramientas de software 
  que incluyan excelentes est&aacute;ndares de calidad y asegurar a nuestros clientes 
  la implantaci&oacute;n de sistemas que cumplen con est&aacute;ndares internacionales.</p>
