<h3>Relacionar Evaluaciones con Cobranza</h3>
<p class="autor"><em>Autor: Maximino Celis Carbajal</em></p>
<p>En casi todas las instituciones educativas existe la necesidad de controlar 
  el pago de algunas cuotas para que los alumnos tengan derecho a presentar alg&uacute;n 
  examen o evaluaci&oacute;n. Un ejemplo claro es el pago por el derecho a presentar 
  exámenes extraordinarios (tambi&eacute;n llamados de regularizaci&oacute;n). 
  Para ello, Control Escolar GES cuenta con una opción que le permitirá <strong>relacionar 
  evaluaciones con cobranza</strong>. De esta manera el colegio de manera automatizada 
  contará con los siguientes beneficios:</p>

  <ul>
    
  <li> En las listas o actas de evaluación, solo aparecer&aacute;n aquellos alumnos 
    que hayan pagado por su examen.</li>
    
  <li> Al momento de registrar calificaciones quedarán bloqueados para su registro 
    los alumnos que no cubrieron su pago. De esta forma se evita el riesgo o margen 
    de error por equivocaci&oacute;n.</li>
  <li> El área de <em>servicios acad&eacute;micos</em> estará en total coordinación 
    con el área de cobranza. Se podrá prescindir de los mecanismos manuales entre 
    estas dos &aacute;reas para notificarse entre s&iacute;, los alumnos deudores 
    y quienes van cubriendo sus pagos paulatinamente.</li>
  </ul>

  
<p>Para realizar el proceso de relación, ingrese al menú CONTROL ACADEMICO y haga 
  clic en la opción [<strong>Relacionar Evaluaciones con cobranza</strong>]. Con 
  esto se presentar&aacute; un asistente que lo guiar&aacute; en todo el proceso.</p>
<p align="center"><img src="<?php echo $url;?>rela_menu.jpg"/></p>
<h4>Paso 1.</h4>
<p>De acuerdo a la modalidad con la cual la institución agrupa a los alumnos, 
  deberá seleccionar la opción GRUPOS o CURSOS para iniciar la relación.</p>
<p align="center"><img src="<?php echo $url;?>rela_p1.jpg"/></p>
<p>Una vez seleccionado haga &quot;clic&quot; en el botón [<em>Siguiente</em>] 
  para continuar.</p>
<h4>Paso 2.</h4>
<p>Elija si desea hacer la relación para un solo grupo o para varios con las siguientes 
  opciones:</p>

  <ul>
    <li> 
      
    <div class="texto"><strong>Por único grupo</strong>. Como se indica solo se 
      utilizar&aacute; un grupo.</div>
    </li>
    <li> 
      
    <div class="texto"><strong>Los grupos de un nivel o sección</strong>. Todos 
      los grupos que est&eacute;n definidos para un nivel o secci&oacute;n.</div>
    </li>
    <li> 
      
    <div class="texto"><strong>Los grupos de un nivel y grado</strong>. Todos 
      los grupos de un nivel y de un solo grado en particular.</div>
    </li>
  </ul>

   <p align="center"><img src="<?php echo $url;?>rela_p2.jpg"/></p>

   
<p>Una vez seleccionada la opción deseada haga &quot;clic&quot; en el botón [<em>Siguiente</em>] 
  para continuar.</p>
<h4>Paso 3.</h4>
<p>A continuación elija cada asignatura que desee relacionar (haciendo &quot;clic&quot;), 
  en otras palabras, en este punto se seleccionan las asignaturas que ser&aacute;n 
  evaluadas pero que ser&aacute;n condicionadas para que solamente sean presentadas 
  por los alumnos que paguen una cuota.</p>
<p align="center"><img src="<?php echo $url;?>rela_p3.jpg"/></p>
<p>Una vez seleccionadas las asignaturas haga clic en el botón [<em>Siguiente</em>] 
  para continuar.</p>
<h4>Paso 4.</h4>
<p>En este paso deberá elegir el criterio de la evaluación que ser&aacute; presentada. 
  En este ejemplo, se ilustra el caso m&aacute;s usual que es el cobro por el 
  derecho a presentar un examen extraordinario o de regularizaci&oacute;n. Aunque 
  también es posible relacionarlo con cualquier otro criterio, por ejemplo el 
  examen final.</p>
<p align="center"><img src="<?php echo $url;?>rela_p4.jpg"/></p>
<p>Al seleccionar alg&uacute;n criterio de evaluación hagamos &quot;clic&quot; 
  en la opci&oacute;n [<em>Siguiente</em>], para continuar.</p>
<h4>Paso 5.</h4>
<p>Finalmente deberemos elegir el plan de pagos y el concepto de cobro que deber&aacute; 
  ser pagado por los alumnos.</p>
<p align="center"><img src="<?php echo $url;?>rela_p5.jpg"/></p>
<p>Una vez seleccionado el plan de pagos y el concepto de cobro haga clic en el 
  botón [Siguiente] para seguir con el proceso.</p>
<p>Nota: Como es obvio, se necesita crear previamente un plan de pagos con el 
  concepto de cobro a relacionar.</p>
<h4>Paso 6.</h4>
<p>En este paso del proceso ya se cuenta con todos los elementos necesarios para 
  realizar la relación. Por lo tanto, solo basta hacer clic en el botón [<em>Terminar</em>] 
  y la relación comenzará a realizarse de manera autom&aacute;tica.</p>
<p align="center"><img src="<?php echo $url;?>rela_p6.jpg"/></p>
<p>Al finalizar el proceso, el sistema dará un aviso de que la relación fue creada 
  y si es el caso, también dará un aviso de la existencia de alguna situación 
  que halla impedido la relación. Los motivos para no crear la relación pueden 
  ser:</p>
<ul>
    
  <li> Una o varias asignaturas ya habían sido relacionadas previamente.</li>
    
  <li> En caso de extraordinarios o regularizaci&oacute;n, uno o varios alumnos 
    han superado el máximo de extraordinarios a presentar por ciclo escolar.</li>
    
  <li> Uno o varios alumnos no tienen derecho a la evaluación relacionada por 
    superar el l&iacute;mite de inasistencias.</li>
  </ul>

    
<h4>Conclusi&oacute;n y Verificación.</h4>
<p>Una vez terminado el proceso, podrá consultar el estado de cuenta de algún 
  alumno y observar&aacute; el concepto y el nombre de la asignatura relacionadas.</p>
