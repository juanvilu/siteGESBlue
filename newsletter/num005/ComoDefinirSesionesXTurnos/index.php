  <h3>Definir sesiones de clase.</h3>
    
  <p align="right" class="autor"><em>Autor: Margarita Lozano Neveros</em></p>
  <p align="justify">Una sesi&oacute;n es un intervalo de tiempo durante el cual 
    se desarrolla una clase, se imparte una asignatura o se desarrolla una actividad 
    no acad&eacute;mica (Por Ejemplo: la hora del receso). Las sesiones pueden 
    diferir en cuanto a su duraci&oacute;n y nombre de un nivel a otro, por ello 
    es necesario configurar las sesiones de forma independiente para cada nivel 
    escolar.</p>
  <p align="justify">Control Escolar GES le permite definir sus sesiones desde 
    el men&uacute; <strong>Configuraci&oacute;n</strong>, submen&uacute; <strong>Datos 
    de la Instituci&oacute;n</strong>. </p>
  <p></p>
  </div>
 
<div class="texto"> 
  <p align="center"><img class=noborder src="<?php echo $url;?>menu.jpg" width="318" height="63" /></p>
  <p align="left">En la ventana de <strong>Configuraci&oacute;n de datos de la 
    Instituci&oacute;n</strong>, seleccionaremos el &iacute;cono Niveles Escolares.</p>
  <p align="center"><img src="<?php echo $url;?>icono_NivelesEscolares.jpg" width="93" height="69"></p>
  <p align="left">Posteriormente, seleccionaremos el nivel previamente creado 
    para el cual le definiremos sus sesiones, dando clic en el bot&oacute;n <strong>Sesiones</strong>.</p>
</div>
<p align="center"><img src="<?php echo $url;?>ConfiguracionDatosInstitucion.jpg" width="700" height="387"></p>
<p align="left">En la ventana de <strong>Definir Sesiones</strong> seleccionaremos 
  el Turno (Vespertino o Matutino), para posteriormente dar clic en el bot&oacute;n 
  <strong>Agregar</strong>.</p>
<p align="left"><br>
  <img class=noborder src="<?php echo $url;?>EditarSesiones.jpg" width="584" height="388"> </p>
<div align="justify" class="texto"> 
  <p>Para definir una Sesi&oacute;n, debemos llenar los campos correspondientes. 
  </p>
  <p><img class=noborder src="<?php echo $url;?>EditarSesion.jpg" width="471" height="256"></p>
</div>
<ul>
  <li> 
    <div align="justify" class="texto"><strong>N&uacute;mero de sesi&oacute;n.</strong> 
      El sistema asigna autom&aacute;ticamente un n&uacute;mero a cada sesi&oacute;n, 
      el usuario no debe preocuparse por este dato.</div>
  </li>
  <li> 
    <div align="justify" class="texto"><strong>Descripci&oacute;n.</strong> Nombre 
      que tendr&aacute; cada sesi&oacute;n. Por Ejemplo: Primer M&oacute;dulo, 
      Receso, Clase #1, etc.</div>
  </li>
  <li> 
    <div align="justify" class="texto"><strong>Hora Inicial.</strong> Horario 
      en el cual dar&aacute; inicio oficialmente la sesi&oacute;n.</div>
  </li>
  <li> 
    <div align="justify" class="texto"><strong>Hora Final.</strong> En relaci&oacute;n 
      con el dato anterior, hora en que concluye la sesi&oacute;n.</div>
  </li>
  <li> 
    <div align="justify" class="texto"><strong>No se imparten clases durante esta 
      sesi&oacute;n.</strong> Marque esta casilla cuando durante esta sesi&oacute;n 
      no se impartan clases por alg&uacute;n motivo. Por ejemplo: recesos.<br>
    </div>
  </li>
</ul>
<p>Al finalizar la captura de nuestra sesi&oacute;n debemos dar clic en el bot&oacute;n 
  <strong>Registrar</strong>.</p>
<p align="justify"><strong>Importante: </strong>El usuario debe cuidar que la 
  hora inicial y final no entren en conflictos con otra sesi&oacute;n. Si una 
  sesi&oacute;n inicia a las 8:00 y concluye a las 9:00 horas, ser&iacute;a incorrecto 
  definir una nueva sesi&oacute;n que iniciar&aacute; a las 9:00 (t&eacute;cnicamente 
  la sesi&oacute;n anterior no ha finalizado). Se recomienda dejar un espacio 
  de un minuto entre sesiones, como se puede apreciar en las siguientes imagenes.</p>
<p><strong>Ejemplo de una sesi&oacute;n para el turno Matutino.</strong></p>
