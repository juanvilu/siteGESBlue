<h2>Bloqueo y Seguimiento al Presupuesto de Ingresos</h2>
<p align="left">Para darle seguimiento al presupuesto y obtener un historial de 
  las cifras que han ido modificando el panorama de ingresos, existe una opción 
  llamada “Seguimiento al Presupuesto”, que informará detalladamente de los cambios 
  que han aumentado o disminuido el presupuesto, tomando como base las cifras 
  bloqueadas.
</p>
   
<p align="left"><strong>Bloquear el Presupuesto</strong>.&nbsp; Esta opción se 
  recomienda realizarla una vez que se asignaron las cuentas por cobrar a los 
  alumnos y emitimos por primera vez nuestro estimado de ingresos al inicio del 
  ciclo escolar, con ello, cualquier movimiento que registren por parte de los 
  alumnos tal como baja, ingreso de nuevos alumnos, o cambio en algun plan, quedará 
  asentado en el seguimiento al presupuesto. </p>
<p><img border="0" src="<?php echo $url;?>bloqueoalpresupuesto.jpg"><br>
  <br>
  <img border="0" src="<?php echo $url;?>bloqueoalpresupuesto2.jpg"> 
</p>
   
<p align="justify">Una vez que se bloquee el presupuesto, el sistema utilizará esta 
  información sin importar los movimientos posteriores que se realicen a las cuentas 
  por cobrar de los alumnos, para revertir esta acción se tiene que desbloquar 
  el presupuesto. <br><br>
Ahora, cualquier movimiento que realicemos a las cuentas por 
cobrar, nos mostrará en el reporte del seguimiento, las variantes que haya sufrido 
nuestro presupento durante el transcurso del ciclo escolar.<br><br> 
</p>
