  <h3>PRESUPUESTO DE INGRESOS</h3>
    
  <p class="autor"><em>Enrique Trujillo García</em></p>
  <p>Dentro de <strong>Control Escolar GES</strong>, existe una opción bastante 
    práctica para conocer el estimado de ingresos por colegiaturas e inscripciones 
    que percibirán las instituciones educativas durante el ciclo escolar, esta 
    se conoce como Presupuesto de ingresos y se encuentra en el menú de cobranza. 
    Esta eficiente herramienta toma los datos estimados en base de los planes 
    de pago asignados a los alumnos al iniciar cada ciclo escolar. </p>
  </div>
  
<p align="center"><img border="0" src="<?php echo $url;?>menu_presupuesto.JPG"> 
<p>Existen dos variantes del presupuesto de ingresos:</p>
<div align="left"> 
  <ul>
    <li>El presupuesto analítico simple, nos proporciona información por nivel 
      educativo o grado o grupo, acerca de las expectativas de captación por cada 
      concepto que aparece en los planes de pago.<br>
      <br>
      Este informe muestra las deducciones que los alumnos que tienen beca, por 
      ello las cifras mostradas son totalmente apegadas a la realidad. Asimismo, 
      para mantener una estimación conservadora de los ingresos, este reporte 
      puede considerar los descuentos por pronto pago.<br>
      <br>
      En este mismo reporte obtenemos un porcentaje de la recuperación sobre el 
      presupuesto total.</li>
    <br>
    <br>
    <li>El presupuesto calendarizado, nos muestra un panorama mensual, respecto 
      a los ingresos esperados por cada concepto.<br>
    </li>
  </ul>
</div>
<h2>¿Cómo obtener los presupuestos de ingresos?</h2>
<p align="justify">Seleccione un filtro para seleccionar todo un grupo, un nivel 
  o sección, un nivel y grado o todos los alumnos existentes en la institución, 
  seleccionando el rango de fechas de las cuales desea obtener el presupuesto. 
  También podrá solicitar el presupuesto por alguno de los conceptos de cobro.</p>
