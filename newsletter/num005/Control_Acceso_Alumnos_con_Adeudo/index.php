  <h3 align="center">Control de acceso a alumnos con adeudo<br>
    M&oacute;dulo de Verificaci&oacute;n de Estado de Cuenta</h3>
  <p class="autor" align="left"><strong>Autor: Roberto A. Romero N&aacute;jera</strong></p>
  <p>Cada instituci&oacute;n educativa seg&uacute;n diversas condiciones particulares, 
    desarrolla sus propios mecanismos para mantener la cartera vencida dentro 
    de l&iacute;mites tolerables. Sin embargo, nunca se podr&aacute; evitar que 
    algunos alumnos incurran en alg&uacute;n tipo de morosidad.</p>
  <p>Una manera de exhortar a los alumnos a cubrir sus adeudos, es utilizando 
    el <em>M&oacute;dulo de Verificaci&oacute;n de Accesos</em> de <em><strong>Control 
    Escolar GES</strong></em>, que se instala en uno o m&aacute;s puntos de ingreso 
    a las instalaciones y con &eacute;l se puede verificar el status de los adeudos. 
    De acuerdo a la normatividad de cada instituci&oacute;n este m&oacute;dulo 
    puede ser solo informativo o bien, se puede optar por impedir el acceso de 
    los alumnos deudores.</p>
  <p align="center"><img src="<?php echo $url;?>acceso.jpg"></p>
  <p>El m&oacute;dulo de Verificaci&oacute;n de Accesos, es una peque&ntilde;a 
    aplicaci&oacute;n independiente que permite introducir la matr&iacute;cula 
    de cada alumno que ingrese a la instituci&oacute;n. La matr&iacute;cula puede 
    teclearse directamente o puede automatizarse utilizando c&oacute;digos de 
    barras o bandas magn&eacute;ticas y lectores &oacute;pticos.</p>
  <p align="center"><strong><img src="<?php echo $url;?>modulo_estado_de_cuenta.jpg"></strong></p>
  <p>Despu&eacute;s de introducir la matr&iacute;cula cada alumno, la aplicaci&oacute;n 
    mostrar&aacute; el status de sus Adeudos y un breve extracto del estado de 
    cuenta.</p>
  <p align="center"><img src="<?php echo $url;?>normal.jpg"></p>
  <p> Para mejorar su impacto gr&aacute;fico, este modulo tiene dos (2) modalidades 
    de visualizaci&oacute;n, <strong>modo normal y modo sem&aacute;foro</strong>. 
    En la modalidad normal solo muestra los movimientos de su estado de cuenta 
    y adeudos que existan, mientras que en la modalidad sem&aacute;foro muestra 
    un icono que indicar&aacute; si el alumno tiene derecho a pasar o no (de acuerdo 
    al adeudo que tenga).</p>
