<ul class="texto">
  <li class="texto"><font size="2" class="texto_tutorial_paso"><a name="planpagos"></a>Planes 
    de Pago</font>. En cada ciclo escolar los planes de pagos pueden contener 
    distintas condiciones, como diferentes cantidades a pagar y distintas fechas 
    de vencimiento, pero probablemente tenga muchas similitudes. En este caso 
    se pueden copiar planes de pago de un ciclo a otro.</li>
</ul>
<table width="95%" border="0" cellpadding="1" cellspacing="3" class="texto">
  <tr> 
    <td>Desde la ventana de Planes de Pago, seleccione un ciclo escolar, plan 
      de pagos y utilizando el men&uacute; contextual haga &quot;clic&quot; en 
      la opci&oacute;n <strong>Copiar plan de Pagos...</strong><br> <br> <img src="<?php echo $url;?>ventana_copia_planespagos.jpg"><br> 
    </td>
  </tr>
  <tr> 
    <td><p>Ahora mu&eacute;vase a otro ciclo escolar...</p>
      <p><img src="<?php echo $url;?>ventana_copia_planespagos_cambiarciclo.jpg" ><strong><br>
        </strong><strong><br>
        </strong></p>
      </td>
  </tr>
  <tr> 
    <td><p>En el nuevo ciclo utilizando el mismo men&uacute; contextual de la 
        lista de planes de pago, haga &quot;clic&quot; en la opci&oacute;n <strong>Pegar 
        plan de pagos</strong>...</p>
      <p><img src="<?php echo $url;?>ventana_copia_planespagos_clicpegar.jpg"></p>
      <p>As&iacute; lucir&aacute; el plan de pagos pegado, que adem&aacute;s mostrar&aacute; 
