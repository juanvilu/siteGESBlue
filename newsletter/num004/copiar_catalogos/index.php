  <h3 align="center">Copiar Cat&aacute;logos e informaci&oacute;n desde otro Ciclo 
    Escolar</h3>
  <p class="autor" align="right"><strong>Autor: Roberto A. Romero N&aacute;jera</strong></p>
  <p>Al momento de preparar un nuevo ciclo, quiz&aacute; podamos identificar ciertas 
    condiciones muy similares que se tuvieron en ciclos escolares pasados y que 
    pueden copiarse para acelerar el arranque o preparaci&oacute;n del nuevo ciclo. 
    Control Escolar GES permite copiar la siguiente informaci&oacute;n desde ciclos 
    anteriores:</p>
<p class="texto">
 <ul class="texto">
    <li><a href="#planestudios" class="ligas">Copiar Planes de Estudio</a>.</li>
    <li><a href="#planpagos" class="ligas">Copiar Planes de Pagos</a>.</li>
    <li><a href="#catgrupos" class="ligas">Duplicar Cat&aacute;logos de Grupos</a>.</li>
    <li><a href="#catcursos" class="ligas">Duplicar Cat&aacute;logos de Cursos</a>.</li>
 </ul>
</p>
</div>
<ul class="texto">
  <li class="texto"><a name="planestudios"></a> <font size="2" class="texto_tutorial_paso">Copiar 
    Planes de Estudios</font>. Considere esta posibilidad si durante el siguiente 
    ciclo se realizar&aacute; alguna renovaci&oacute;n de un plan de estudios.</li>
</ul>
