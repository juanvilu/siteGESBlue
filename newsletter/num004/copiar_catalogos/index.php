      el men&uacute; contextual (oprimiendo clic con el bot&oacute;n derecho del 
      mouse) haga &quot;clic&quot; en la opci&oacute;n <strong>Copiar plan de 
      Estudios...</strong><br>
      <br>
      <img src="<?php echo $url;?>ventana_copia_planestudio.jpg"><br>
    </td>
  </tr>
  <tr>
    <td>Con el mismo men&uacute; contextual, haga &quot;clic&quot; en <strong>Pegar 
      Plan de Estudios...<br>
      <br>
      </strong>Como resultado, observar&aacute; una copia del plan original que 
      conservar&aacute; una estructura identica al original incluyendo asignaturas 
      y criterios de evaluaci&oacute;n.<strong><br>
      </strong></td>
  </tr>
  <tr>
