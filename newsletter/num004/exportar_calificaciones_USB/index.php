  <h3 align="center">Captura de calificaciones usando memorias USB</h3>
    
  <p class="autor" align="right"><em>Autor: Roberto A. Romero N&aacute;jera</em></p>
  Desde hace mucho tiempo<strong> Control Escolar GES</strong> ha permitido la 
  utilizaci&oacute;n de disquetes para capturar calificaciones en cualquier PC, 
  esto ha sido muy &uacute;til cuando es necesario que los profesores u otras 
  personas apoyen en el registro de calificaciones.
<p align="left"> Ahora ya es posible utilizar una memoria USB 
    para este proceso. Esta opción es muy útil, pues permite que cualquier PC, 
    sin necesidad de instalar aplicaciones, se transforme en una &quot;terminal 
    de captura de calificaciones&quot;; una vez capturadas, se pueden cargar de 
    una forma sencilla y rápida.</p>

  <table border="0">
    <tr> 
      <td width="38%" height="112"><img src="<?php echo $url;?>memorias_usb.jpg" class="noborder"></td>
      <td width="62%" class="texto">Las memorias USB se han convertido en el dispositivo 
        ideal para intercambiar y trasladar información, permite almacenar más 
        información de manera segura, ágil y portátil. Estas memorias son resistentes 
        a los rasguños y al polvo que han afectado a las formas previas de almacenamiento 
        portátil, como los CD y los disquetes. Así, quedarán atrás las molestias 
        causadas por los disquetes, los cuales generalmente fallan cuando más 
        se necesitan. La capacidad de estos dispositivos ha ido evolucionando, 
        desde los 8 MB en sus inicios hasta varios Gigas de hoy en día.</td>
    </tr>
  </table>
  <h4><b> Proceso para trasladar las calificaciones a memoria USB.</b> </h4>
  Se utiliza la opción de <strong>Registro de Calificaciones por Grupo...</strong> 
  del menú de Control Académico.
<p align="left"><strong><font size="2" class="texto_tutorial_paso">Paso 
    1.</font></strong> Ahi seleccionaremos la asignatura a exportar y hacemos 
    &quot;clic&quot; sobre la opci&oacute;n "Exportar...&quot;</p>
  <p align="center"><img src="<?php echo $url;?>botones_exportacion_importacion.jpg" class="noborder"> 
  </p>
  <p align="left"><b>Nota:</b> Antes de iniciar con el proceso de 
    Exportación debemos tener nuestra memoria USB ya conectada a la PC, de lo 
    contrario la exportación se hará hacia la unidad de disquete directamente.</p>
  <p align="left"><strong><font size="2" class="texto_tutorial_paso">Paso 2</font>:</strong> 
    Ahora seleccionaremos las calificaciones que deseamos trasladar a la memoria 
    USB. Marque los criterios de evaluaci&oacute;n necesarios.</p>
  <table width="76%" border="0">
    <tr> 
      <td width="37%" class="texto"> Aparecer&aacute; una ventana de selecci&oacute;n 
        de criterios a capturar. Para este ejemplo seleccionamos el criterio <strong>PAR2</strong>.<br> <br> <img src="<?php echo $url;?>seleccion_criterios.jpg" width="262" height="212"></td>
      <td width="5%" class="texto">&nbsp;&nbsp; </td>
      <td width="37%" valign="top" class="texto"> Tambi&eacute;n se podrían seleccionar 
        más criterios de acuerdo a las necesidades. <br>
        <br>
        <br>
        <div align="right"><img src="<?php echo $url;?>seleccion_criterios_varios.jpg" width="262" height="212"></div></td>
    </tr>
  </table>
  <p><strong><font size="2" class="texto_tutorial_paso">Paso 3</font>:</strong> 
    Ahora seleccionaremos la unidad que contiene la memoria USB:<br>
    <br>
    <img src="<?php echo $url;?>seleccion_unidad.jpg" width="250" height="223"></p>
  <p><strong><font size="2" class="texto_tutorial_paso">Paso 4</font>:</strong> 
    Enseguida nos pedirá asignar un password. Este password servir&aacute; para 
    validar al usuario autorizado al momento de iniciar la captura de calificaciones; 
    de lo contrario no podrá efectuar la captura. <br>
    <br>
    <img src="<?php echo $url;?>password.jpg" width="244" height="163"> </p>
  Si hemos seleccionado más de un criterio de evaluaci&oacute;n para copiar, el 
  sistema enviar&aacute; el siguiente di&aacute;logo de confirmaci&oacute;n. Donde 
  si elegimos SI, todos los criterios exportados se podrán capturar, en caso de 
  elegir NO solo el último criterio se podrá capturar, los otros ser&aacute;n 
  de &quot;solo lectura&quot;.<br>
  <br>
  <img src="<?php echo $url;?>mensaje_criterios.jpg" width="418" height="100"> 
  <p align="left">Acto seguido se generan los archivos necesarios en nuestra memoria 
    (o disquete), para que se puedan capturar las calificaciones en cualquier 
    PC.</p>
  <h4 align="left"><b>Proceso de Captura de calificaciones en cualquier computadora</b></h4>
  <p><font size="2" class="texto_tutorial_paso">Paso 5:</font> Ahora el profesor 
    o la persona propietaria de la memoria USB, deber&aacute; conectar la memoria 
    en el puerto USB de la PC y ejecuta la mini-aplicaci&oacute;n Registro.exe 
    del directorio raíz de la memoria. </p>
  <p>Al ejecutar la mini-aplicaci&oacute;n se abrir&aacute; una ventana con las 
    asignaturas que est&aacute;n contenidas en la memoria, aqu&iacute; deberemos 
    seleccionar la asignatura que vamos a capturar. Si tiene un password asignado 
    nos lo pedir&aacute;. </p>
  <p><img src="<?php echo $url;?>seleccion_asignatura_registrar.jpg" width="321" height="236"> 
    <font size="2" class="texto_tutorial_paso"><br>
    <br>
    Paso 6:</font> Ahora proceda a capturar las calificaciones, y no olvide guardarlas 
    antes de cerrar la aplicaci&oacute;n. </p>
  <p>Al utilizar la mini-aplicaci&oacute;n Registro de Calificaciones, podremos 
    capturar parcialmente las calificaciones y posteriormente concluirlas en cualquier 
    momento y en cualquier PC.<br>
    <br>
  </p>
