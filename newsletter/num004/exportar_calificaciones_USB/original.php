      <td width="62%">Las memorias USB se han convertido en el dispositivo ideal 
        para intercambiar y trasladar información, permite almacenar más información 
        de manera segura, ágil y portátil. Estas memorias son resistentes a los 
        rasguños y al polvo que han afectado a las formas previas de almacenamiento 
        portátil, como los CD y los disquetes. Así, quedarán atrás las molestias 
        causadas por los disquetes, los cuales generalmente fallan cuando más 
        se necesitan. La capacidad de estos dispositivos ha ido evolucionando, 
        desde los 8 Megas en sus inicios hasta varios Gigas de hoy en día.</td>
    </tr>
