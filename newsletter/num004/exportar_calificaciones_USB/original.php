    <p align="left"><b>Importación de las Calificaciones. </b></p>
    <p align="left">Si los datos esta en memoria USB es necesario introducirla 
      en el puerto antes de efectuar el proceso de Importación; si esta un disquete, 
      por excepción el sistema accesa a la unidad 3 ½".</p>
    <p align="left">Seleccionamos el grupo al que se le importaran las calificaciones 
      en la opción de Registro de Calificaciones por Grupo y Asignatura del menú 
      de Control Académico; seleccionamos alguna asignatura, (no es necesario 
      seleccionar la asignatura a importar, ya que el sistema importa los datos 
      sobre la asignatura que corresponda).</p>
    <p align="left">Presionamos el botón de Importación, nos aparecerá el mismo 
      cuadro de dialogo de selección de unidad, para nuestro caso seleccionas 
      la unidad extraíble (memoria USB), le damos Si al mensaje de advertencia. 
      Si previamente se exportaron a la memoria USB varias materias de un grupo 
      o de diferentes grupos, el sistema mostrará solo las materias del grupo 
      en operación:</p>
