    <p align="left">En caso de haber seleccionado más de un criterio de captura 
      el sistema nos preguntará:</p>
    <p align="center"><img src="<?php echo $url;?>mensaje_criterios.jpg" width="418" height="100"></p>
    <p align="left">Es decir, si damos SI, todos los criterios exportados se podrán 
      capturar; si le damos NO, solo el último criterio se podrá capturar, los 
      primeros solo se podrán consultar más no modificar.</p>
    <p align="left">Acto seguido se generan los archivos necesarios en nuestra 
      memoria o disquete, para que se puedan capturar las calificaciones en cualquier 
      PC que tenga un puerto USB.</p>
    <p align="left">&nbsp;</p>
    <p align="left"><b>Proceso de Captura. </b></p>
    <p align="left">Ahora el profesor conecta su memoria en la PC y solamente 
      se ejecuta el archivo Registro.exe del directorio raíz de nuestra memoria 
      USB. </p>
    <p align="left">Se abre una ventana con las asignaturas que se le exportaron 
      al profesor y se selecciona la asignatura a capturar. Enseguida nos pedirá 
      el password dado de alta en la exportación. </p>
    <p align="center"><img src="<?php echo $url;?>seleccion_asignatura_registrar.jpg" width="321" height="236"></p>
    <p align="center">&nbsp;</p>
    <p align="left">Se procede a la captura de calificaciones y se guardan.</p>
