  <h3 align="center">Exportaci&oacute;n de calificaciones (memoria USB)</h3>
    
  <p class="autor" align="right"><em><font size="2">Por Roberto A. Romero N&aacute;jera.<br>
    roberto_arn@grupoges.com.mx </font></em></p>
  <p class="autor" align="left">Control Escolar GES permite la exportación de 
    calificaciones a disquetes para su captura en cualquier pc. Pero ahora ya 
    es posible exportaro tambien a memoria USB. Esta opción del sistema es muy 
    útil, porque permite que en cualquier PC que se tenga al alcance, se pueda 
    efectuar la captura de calificaciones por parte del profesor y una vez capturadas 
    cargarlas al sistema de una forma sencilla y rápida.<br>
  </p>

  <table width="85%" border="0">
