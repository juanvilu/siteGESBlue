  <h3>Control Escolar GES cambia a Firebird.</h3>
  <p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
  <p align="justify">En Grupo GES buscando el mejor beneficio y econom&iacute;a 
    para nuestros clientes, hemos desarrollado nuestros productos utilizando soporte 
    de herramientas de fuente abierta (open-source). Por ese motivo, hemos utilizado 
    como base de datos a Borland Interbase bajo la licencia de fuente abierta.</p>
  <p align="justify">Lamentablemente, a partir de 2004 la empresa propietaria 
    de Interbase volv&iacute;o a retomar la comercializaci&oacute;n del producto 
    dejando abierta solamente la versi&oacute;n 6, lo cual ha impedido el crecimiento 
    y adaptabilidad de Interbase a las mejoras tecnol&oacute;gicas presentes en 
    los nuevos equipos de c&oacute;mputo, particularmente los procesadores con 
    doble n&uacute;cleo (core duo).</p>
  <p align="justify">Sin embargo, derivada de la versi&oacute;n open-source de 
    Interbase se gener&oacute; un movimiento liderado por un grupo de desarrolladores 
    internacionales quienes desarrollaron una nueva base de datos de fuente abierta 
    llamada Firebird SQL, misma que ha venido progresando considerablemente e 
    incluye innovaciones tecnol&oacute;gicas importantes, y lo mejor de todo es 
    que sigue siendo aplicable a m&uacute;ltiples plataformas como Windows, Linux, 
    Novell, Mac OS X, etc.</p>
  <p align="justify">Despu&eacute;s de largas etapas de pruebas, Grupo GES tiene 
    el agrado de anunciar que a partir del mes de Abril de 2007, los productos 
    Control Escolar GES tendr&aacute;n a <strong>Firebird versi&oacute;n 1.5</strong> 
    como plataforma de base de datos predeterminada.</p>
  <h3 align="justify">Ventajas de Firebird</h3>
</div>
<ul>
  <li> Practicidad.</li>
  <li> F&aacute;cil de instalar.</li>
  <li> Facilidad en el manejo.</li>
  <li>Gran rapidez en su operaci&oacute;n.</li>
  <li>Aprovechamiento de la capacidad de multiprocesamiento encontrada en los 
    equipos de c&oacute;mputo modernos.</li>
  <li>Compatibilidad con Interbase para una migraci&oacute;n transparente y r&aacute;pida, 
    desde versiones anteriores.</li>
  <li>Alta integraci&oacute;n con otras herramientas de fuente abierta como Apache 
    Web Server y PHP.</li>
  <li>Iguala e incluso supera en la funcionalidad a muchos de sus equivalentes 
    comerciales.</li>
</ul>
<p>La adopci&oacute;n de Firebird contribuye adem&aacute;s a generar un instalador 
  m&aacute;s amigable para nuestros usuarios quienes ahora gozar&aacute;n de una 
  interface de instalaci&oacute;n m&aacute;s eficiente desde el primer momento.</p>
