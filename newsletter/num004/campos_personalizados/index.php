  <h3>Campos personalizados para profesores y alumnos.</h3>
    
  <p align="right" class="autor"><em>Autor: Margarita Lozano Neveros</em></p>
  <p align="justify">Control Escolar GES proporciona una ficha de datos para registro 
    de alumnos muy completa, sin embargo, a pesar de eso algunos usuarios requieren 
    datos particulares de uso exclusivo para su instituci&oacute;n.</p>
  <p align="justify">Para dar respuesta a esas necesidades, Control Escolar GES 
    contiene un mecanismo por medio del cual cada instituci&oacute;n puede agregar 
    esos campos de uso particular tanto para la <em>ficha de Alumnos</em> como 
    para la <em>ficha de Profesores</em>. </p>
  <p align="justify">Para agregar nuevos campos; ingresaremos al men&uacute; <strong>Configuraci&oacute;n 
    </strong>y a la opci&oacute;n <strong>Datos de la Instituci&oacute;n.</strong></p>
  <p></p>
  </div>
 
<div class="texto"> 
  <p align="center"><img src="<?php echo $url;?>menu.jpg" width="272" height="60" /></p>
  <p align="left">Observe que la<strong> </strong> pesta&ntilde;a <strong>Datos 
    Adicionales, </strong>contiene una secci&oacute;n llamada &quot;Datos Personalizados 
    para cada Instituci&oacute;n&quot;.</p>
  <p align="center"><img src="<?php echo $url;?>configuracion.jpg" width="674" height="266"></p>
  <p align="left">Para agregar un campo personalizado; debemos dar clic en el 
    siguiente signo <img src="<?php echo $url;?>agregar.jpg" width="39" height="29"> 
    y llenar los campos correspondientes.</p>
  <p align="center"><img src="<?php echo $url;?>configuracion_campo.jpg" width="481" height="223"></p>
</div>
<ul>
  <li> <strong>Identificador &uacute;nico del Campo: </strong>Ser&aacute; un n&uacute;mero 
    consecutivo generado automaticamente.</li>
  <li> <strong>Descripci&oacute;n o Nombre del Campo:</strong> Es el nombre con 
    el que quedar&aacute; registrado el dato adicional y deber&aacute; teclearse 
    con may&uacute;sculas y sin espacios. Puede utilizar gui&oacute;n bajo &quot;_&quot; 
    para sustituir los espacios.</li>
  <li><strong>Tipo de Dato que utilizar&aacute;: </strong>Escoja entre Alfanum&eacute;rico, 
    Num&eacute;rico o Fecha. 
    <blockquote> 
      <p><em>Alfanum&eacute;rico. </em>Permite incorporar texto normal.<br>
        <em>Num&eacute;rico (Entero). </em>N&uacute;meros enteros. P.e. 20, 30, 
        44, 3322, etc.<br>
        <em>Num&eacute;rico Largo (con punto decimal).</em><strong> </strong>N&uacute;meros 
        precisos incluyendo representaciones con punto decimal. P.e. Cantidades 
        monetarias.<br>
        <em>Fecha.</em> Fechas con representaci&oacute;n en t&eacute;rminos de 
        d&iacute;a, mes y a&ntilde;o.</p>
    </blockquote>
  </li>
  <li><strong>Etiqueta que utilizar&aacute; al capturar: </strong>Descripci&oacute;n 
    que se mostrar&aacute; al momento de realizar la captura de este dato (en 
    la ficha del alumno).</li>
  <li><strong align="left">Longitud</strong>: Establece la cantidad m&aacute;xima 
    de caracteres que podr&aacute;n contener los campos de tipo alfanum&eacute;rico.<br>
  </li>
</ul>
<p>Una vez llenados los campos; damos clic en el bot&oacute;n <strong>Registrar 
  Campo,</strong> para que quede registrado. Se sugiere salir del sistema y volver 
  a ingresar para activar los nuevos campos.</p>
<p align="justify">Finalmente, se ver&aacute;n reflejados los campos en la ficha 
  general del alumno dentro de la pesta&ntilde;a <strong>Personalizados</strong>. 
</p>
<p align="center"> <img src="<?php echo $url;?>campo_enfichaalumno.jpg" width="677" height="173"> 
</p>
<p align="justify">Los campos son alimentados por captura directa, lo cual deja 
  en libertad a los usuarios para introducir cualquier valor, por ello, la informaci&oacute;n 
  puede llegar a contener algunas inconsistencias por error de captura. Para evitar 
  dichos errores, se ha incorporado la capacidad para utilizar un soporte de cat&aacute;logos, 
  pero eso ser&aacute; materia de una futura discusi&oacute;n. </p>
