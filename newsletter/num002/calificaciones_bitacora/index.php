  <h3>Registro de movimientos de calificaciones en bitácora de usuarios</h3>
    
  <p class="autor"><em>Autor: Maximino Celis Carbajal</em></p>
    
  <p>En <strong>Grupo GES</strong> estamos conscientes de la importancia de contar 
    con un registro de calificaciones confiable y seguro, para ello, <strong>Control 
    Escolar GES</strong> en su versión 4.0 cuenta con la posibilidad de auditar 
    los registros y movimientos de calificaciones que se hagan a través del sistema.</p>
  </div>
 
<div class="texto"> 

  <h4>1. Como activar el registro de movimientos en calificaciones.</h4>

    
  <p>Ingrese al menú "CONFIGURACION" y elija la opción "Datos de la Institución".</p> 

    <p align="center"><img src="<?php echo $url;?>menu_datosinst.jpg" /></p>

    <p>En la pantalla que se despliega seleccione la pestaña "Interfaz de Usuario". En la sección de "Datos de Comportamiento General" active la casilla de verificación llamada "GUARDAR CAMBIOS DE CALIFICACIONES EN BITACORA DE EVENTOS".</p>

    <p align="center"><img src="<?php echo $url;?>interfaz_usuario.jpg" /></p>
	
    
  <p>Una vez marcada la casilla, haga clic en &quot;<strong>Guardar cambios</strong>&quot; 
    para que el sistema tome la nueva configuración. A partir de este momento, 
    quedar&aacute; registro de cualquier movimiento de captura de calificaciones.</p>

  <h4>2. Dónde consultar los movimientos de calificaciones.</h4>	

    
  <p>Para poder hacer una auditor&iacute;a de los movimientos de calificaciones, 
    debe ingresar a la opción "Bitácora de Actividades de Usuarios" del menú "CONFIGURACION".</P>

    <p align="center"><img src="<?php echo $url;?>menu_bitacora.jpg" /></p>

    
  <p>En la ventana que se despliega ingrese un rango de fechas para hacer la búsqueda. 
    De manera opcional puede elegir un usuario o una actividad en específico para 
    hacer la b&uacute;squeda.</p>
  
    <p align="center"><img src="<?php echo $url;?>filtros_usuarios.jpg" /></p> 
 
    
  <p>A continuación se desplegará el listado con la información de las actividades 
    realizadas por los usuarios dentro del sistema. Entre ellas podrá visualizar 
    los movimientos de calificaciones. Los datos que servir&aacute;n para una 
    auditor&iacute;a son el nombre de usuario, el día y hora, la actividad realizada, 
    algun detalle de la actividad y la estación de trabajo desde la que se accedi&oacute; 
    al sistema.</p>

    
  <p><strong>EJEMPLO 1.</strong> Calificaciones registradas en asignaturas por 
    primera vez.</p>

    <p align="center"><img src="<?php echo $url;?>bitacora_registrada.jpg" /></p>

    
  <p>En este ejemplo podemos ver que el usuario "admin", el día 02/09/2006, a 
    las 10:36 am, capturó las calificaciones de varios alumnos. En el detalle 
    nos especifíca el codigo de la materia, la evaluación afectada y la calificación 
    asentada.</p>
  <p><strong>EJEMPLO 2.</strong> Calificaciones modificadas en asignaturas.</p>

    <p align="center"><img src="<?php echo $url;?>bitacora_modificada.jpg" /></p>

    
  <p>En este segundo ejemplo podemos ver que el usuario "admin", el día 10/11/2006, 
    a las 10:23 am, modificó las calificaciones de dos alumnos. En el detalle 
    nos especifíca que modificó en un alumno una calificación de 9 a 8 y en otro 
    de 9 a 10.</p>

