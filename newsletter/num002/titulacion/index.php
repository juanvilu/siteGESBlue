    la naturaleza de la profesi&oacute;n y de las necesidades sociales que se 
    trate de satisfacer, exigir&aacute;n a los estudiantes de las profesiones 
    a que se refiere esta Ley como requisito previo para otorgarles el t&iacute;tulo, 
    que presten servicio social durante el tiempo no menor de seis meses ni mayor 
    de dos a&ntilde;os.&#8221; <br>
