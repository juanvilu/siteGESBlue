  <h4>Resumen del M&oacute;dulo para Administraci&oacute;n de Titulaci&oacute;n.</h4>
  <p>En este m&oacute;dulo se propone un procedimiento general para la administraci&oacute;n 
    de todas las actividades referentes a la titulaci&oacute;n:</p>
  <p><img src="<?php echo $url;?>grafico.jpg" class="noborder"></p>
  <p>De acuerdo a este procedimiento, se han considerado los siguientes aspectos 
    b&aacute;sicos: </p>
  <ul>
    <li> <strong>Manejo de m&uacute;ltiples opciones de Tituaci&oacute;n por especialidad</strong>. 
      Cada instituci&oacute;n puede configurar con libertad las diferentes modalidades, 
      as&iacute; como los requisitos que los alumnos deben cumplir en cada una 
      de ellas.<br>
      <br>
      <img src="<?php echo $url;?>ventana_modalidades_requisitos.gif" class="noborder"><br>
    </li>
    <li> <strong>Integraci&oacute;n del expediente de Titulaci&oacute;n</strong>. 
      Por cada alumno deber&aacute; abrirse un expediente en donde se controla 
      la modalidad que seleccion&oacute;, el cumplimiento de los requisitos, asignaci&oacute;n 
      de tema para trabajo recepcional, avance del status del trabajo y asesor 
      para trabajo recepcional.<br>
      <br>
      <img src="<?php echo $url;?>ventana_ficha_titulacion.gif" class="noborder"> 
      <br>
    </li>
    <li><strong>Examen Recepcional</strong>. Al concluir la elaboraci&oacute;n 
      del trabajo recepcional podr&aacute; agendar el examen recepcional, incluyendo 
      fecha y lugar, asignaci&oacute;n de jurado y por supuesto registro del Resultado 
      o Dictamen.<br>
      <br>
      <img src="<?php echo $url;?>ventana_examen_recepcional.gif" class="noborder"> 
      <br>
    </li>
  </ul>
  <p>Para complementar el proceso anterior, el m&oacute;dulo de Titulaci&oacute;n 
    cuenta con un apartado donde se registra la prestaci&oacute;n del servicio 
    social (o comunitario), as&iacute; como las pr&aacute;cticas profesionales 
    o residencia profesional.</p>
  <p>Esto es solo una breve menci&oacute;n de las caracter&iacute;sticas de este 
    importante <strong>m&oacute;dulo</strong>, que est&aacute; disponible para 
    todos los usuarios de Control Escolar GES sin costo adicional.</p>
