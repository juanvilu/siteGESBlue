  <h4><strong>Consejos para la impresi&oacute;n y correcta lectura de c&oacute;digos 
    de barras.</strong></h4>
  <p>Seguramente hemos sido testigos de situaciones en donde un c&oacute;digo 
    de barras no puede ser le&iacute;do por un lector o scanner. Muchos factores 
    pueden afectar la confiabilidad de estos c&oacute;digos.</p>

  <ul>
    <li> Tratar de leer un c&oacute;digo de barras con un lector que no aceptar&aacute; 
      el tipo de c&oacute;digo que est&aacute; siendo utilizado. (Recuerde, que 
      el proceso de usar c&oacute;digos de barras requiere de dos tareas &#8211; 
      impresi&oacute;n y lectura &#8211; y la impresora solo afecta una de ellas). 
    </li>
    <li> Utilizar un medio de impresi&oacute;n inadecuado para el trabajo. P.e. 
      Pl&aacute;sticos, vidrios, superficios rugosas, entre otros.</li>
    <li> Imprimir c&oacute;digos de barras muy estrechos, donde los espacios entre 
      las barras sean imposibles de interpretar por el lector..</li>
    <li> Todas las reglas de simbolog&iacute;a de c&oacute;digos de barras deben 
      seguirse cuando se impriman c&oacute;digos de barras. Por ejemplo, no se 
      pueden imprimir caracteres alfanum&eacute;ricos al usar tipos de c&oacute;digos 
      que aceptan solo n&uacute;mero; el hacer esto se obtendr&aacute;n c&oacute;digos 
      incorrectos. </li>
    <li>Siempre confirme que el tipo de c&oacute;digo que desea usar soporte los 
      caracteres que desea codificar.</li>
    <li>No imprima c&oacute;digos de barras en color, muchos lectores no distinguen 
      los colores.</li>
    <li>Imprime c&oacute;digos de barras tan grandes como sea posible.</li>
  </ul>

  <h4><strong>Ejemplo del uso de un c&oacute;digo de barras en un reporte</strong></h4>
  <p>Tomaremos como ejemplo un formato de credenciales de alumnos. </p>
  <p>Para agregar un c&oacute;digo de barras debemos insertar una expresi&oacute;n 
    que utilice la palabra especial BARCODE. La sintaxis es BARCODE=&lt;dato&gt;, 
    por ejemplo, BARCODE=MATRICULA si deseamos codificar la MATRICULA de un alumno.</p>
  <p><img src="<?php echo $url;?>imagen2.jpg" class="noborder"></p>
  <p>En la imagen anterior se puede ver la expresi&oacute;n que se ha agregado, 
    solo basta hacer los ajustes relativos al tama&ntilde;o del c&oacute;digo 
    de barras (alto y ancho). A partir de ahora ya se imprimir&aacute; un c&oacute;digo 
    de barras, usando el tipo por &quot;default&quot; que asignar&aacute; el sistema.</p>
  <p>Si desea un tipo de c&oacute;digo de barras espec&iacute;fico, deber&aacute; 
    ind&iacute;carlo haciendo &quot;clic&quot; con el bot&oacute;n derecho del 
    mouse sobre la expresi&oacute;n BARCODE, ahi seleccione la opci&oacute;n &#8220;Definir 
    mascara de salida&#8221; del men&uacute; emergente e indique el tipo de c&oacute;digo 
