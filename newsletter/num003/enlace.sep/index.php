  <h3>El programa Enlace del gobierno mexicano.</h3>
  <p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
  <table width="95%" border="0" cellspacing="3" cellpadding="1">
    <tr>
      <td><img src="<?php echo $url;?>sep.gif" width="80" height="39" class=noborder></td>
      <td class="texto">En Junio del a&ntilde;o 2006, m&aacute;s de 8.3 millones de estudiantes 
        mexicanos de educaci&oacute;n b&aacute;sica (primaria y secundaria) fueron 
        sometidos a una evaluaci&oacute;n por parte de la Secretar&iacute;a de 
        Educaci&oacute;n P&uacute;blica (SEP). A partir de Octubre del mismo a&ntilde;o, 
        la SEP abri&oacute; a toda la poblaci&oacute;n los resultados de la dicha 
        evaluaci&oacute;n. </td>
    </tr>
  </table>
  <p align="justify">Los resultados est&aacute;n disponibles a trav&eacute;s de 
    Internet en la direcci&oacute;n <a href="http://enlace.sep.gob.mx" target="_blank"><u>enlace.sep.gob.mx</u></a> 
    y pueden ser consultados tanto por padres de familia como por los propios 
    alumnos. Cabe aclarar que esta evaluaci&oacute;n solo est&aacute; siendo aplicada 
    a ni&ntilde;os y j&oacute;venes de 3&ordm; a 6&ordm; grados de educaci&oacute;n 
    primaria y 3&ordm; de educaci&oacute;n secundaria.</p>
  <p align="justify">A&uacute;n se desconoce si a futuro la evaluaci&oacute;n 
    ser&aacute; aplicada para 1&ordm; a 2&ordm; grados de primaria y 1&ordm; a 
    2&ordm; de secundaria.</p>
  <p>Con este servicio, la SEP intenta involucrar a los padres de familia en la 
    supervisi&oacute;n del desempe&ntilde;o acad&eacute;mico de los alumnos, lo 
    cual parece ser un objetivo dif&iacute;cil toda vez que el &iacute;ndice de 
    familias que cuentan con conexi&oacute;n a Internet todav&iacute;a es bajo 
    en la poblaci&oacute;n mexicana.</p>
  <p>Para realizar la consulta de resultados el pap&aacute; o alumno deber&aacute; 
    contar con el n&uacute;mero de folio que le fue proporcionado cuando realiz&oacute; 
    su evaluaci&oacute;n. Si no cuenta con esta informaci&oacute;n, puede recurrir 
    a una b&uacute;squeda por nombre del alumno, o bien por n&uacute;mero del 
    centro de trabajo ubicado en la boleta de calificaciones del alumno.</p>
</div>
<div class="texto"> 
  <p>Tambi&eacute;n para docentes y directivos el programa presenta informaci&oacute;n 
    importante:</p>
</div>
<ul>
  <li> 
    <div class="texto"><strong>Como docente</strong>: conocer en qu&eacute; contenidos 
      y con cu&aacute;les alumnos se requiere reforzar el trabajo y la atenci&oacute;n, 
      as&iacute; como evaluar objetivamente sus estrategias de ense&ntilde;anza.</div>
  </li>
  <li> 
      
    <div class="texto"><strong>Como directivo</strong>: analizar los resultados 
      por alumno, por grupo y globales de la escuela, para facilitar la reflexi&oacute;n 
      y discusi&oacute;n colectiva que permita identificar pr&aacute;cticas pedag&oacute;gicas 
      efectivas y establecer estrategias para atender a los ni&ntilde;os que se 
      ubican en el nivel de logro &#8220;insuficiente&#8221;.</div>
  </li>
</ul>
<div class="texto">
  <p>La SEP a&uacute;n no ha logrado solventar todos los problemas inherentes 
    a una tarea de semejante magnitud, por ejemplo:</p>
</div>
<ul>
  <li> 
    <div class="texto">No todas las escuelas participaron ni todos los alumnos, 
      por lo que es posible que al intentar localizar a alg&uacute;n alumno o 
      plantel educativo, el sistema no lo tenga registrado. </div>
  </li>
  <li> 
    <div class="texto">Solo 18 entidades federativas enviaron una base de datos 
      con informaci&oacute;n personalizada por alumno. En el resto de las entidades, 
      se escane&oacute; el nombre tal como lo escribi&oacute; el alumno, existiendo 
      la posibilidad de que al no contar con el folio e intentar ingresar con 
      el nombre del alumno, el sistema no lo logre identificar.</div>
  </li>
</ul>
<div class="texto">
  <p>De acuerdo con los primeros resultados, tres de cada 100 niños que cursan 
    la primaria tuvieron nivel de excelencia y sólo uno de cada centenar en secundaria 
    obtuvo una calificación similar. </p>
  <p>La evaluaci&oacute;n se volver&aacute; a aplicar el pr&oacute;ximo mes de 
    abril y la SEP espera cubrir un mayor n&uacute;mero de escuelas de nivel b&aacute;sico 
    en todo el pa&iacute;s, con el fin de contar con una mejor y m&aacute;s profunda 
    evaluaci&oacute;n de los estudiantes. </p>
</div>
<div class="valoracion"> 
  <p><b>Nota Importante:</b> Esta evaluaci&oacute;n ser&aacute; aplicada cada ciclo escolar en el mes de 
    abril, cuando se realiza la Semana Nacional de Evaluaci&oacute;n.<br>
