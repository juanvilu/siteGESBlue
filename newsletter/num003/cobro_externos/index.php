  <h3>Como cobrar a una persona externa.</h3>
    
  <p align="right" class="autor"><em>Autor: Margarita Lozano Neveros</em></p>
  <p align="justify">En algunas Instituci&oacute;nes se tiene la necesidad de 
    expedir recibos de pago por productos o servicios a usuarios que no est&aacute;n 
    registrados en nuestra base de datos, Control Escolar GES cuenta con esta 
    funcionalidad; para lograrlo debemos seguir dos sencillos pasos.</p>
  <p align="justify"><strong>Paso 1: Preparar el sistema para recibir cobros de 
    personas externas.</strong><strong></strong></p>
  <p align="justify">Por default, Control Escolar GES solo permite registrar cobros 
    por alumno o por familia, por tal motivo deberemos preparar el sistema para 
    que acepte cobros tambi&eacute;n a personas externas.</p>
  <p align="justify">Solicite a su proveedor o descargue aqu&iacute; la utiler&iacute;a 
    <a href="ftp://usuarios:escolarges@www.grupoges.com.mx/Utilerias/CobroExternos.zip">CobroExternos.zip</a> 
    y corra el archivo ejecutable que viene dentro. Haga &quot;clic&quot; en la 
    opci&oacute;n &quot;Habilitar los par&aacute;metros...&quot;</p>
  <p align="justify"><strong><img src="<?php echo $url;?>utileria_cobro_externos.jpg"></strong></p>
</div>
<div class="texto">
  <p align="justify">Al concluir, la utiler&iacute;a habr&aacute; aplicado los 
    cambios necesarios para realizar nuestro primer cobro a personas externas.</p>
</div>
<div class="texto">
  <p align="justify"><strong>Paso 2: </strong>Al concluir los pasos anteriores, 
    ya tendremos la opci&oacute;n de cobrar a una persona externa, es decir, en 
    la ventana de pagos adem&aacute;s de las modalidades de cobro por alumno y 
    familia ahora aparecer&aacute; la opci&oacute;n <strong>&quot;Persona Externa&quot;.</strong></p>
  <p align="center"><strong><img src="<?php echo $url;?>ConNvaForma.jpg"></strong></p>
  <p align="justify"><strong>Ejemplo de cobro a una persona externa.</strong></p>
  <p align="justify"> Introduciremos los datos para el registro y control del 
    pago (fecha y folio de recibo), seleccionaremos como forma de cobro &quot;Persona 
    Externa&quot; y haremos clic en la opci&oacute;n <strong>Siguiente Pago.</strong></p>
  <p align="justify">Al cobrar a personas externa deberemos teclear el <strong>Nombre 
    de la persona</strong> que realiza el pago (por default aparecer&aacute; la 
    palabra EXTERNO).<br>
    <br>
    <img src="<?php echo $url;?>RegistroIngresos1.jpg"> <br>
    <br>
    Despu&eacute;s de teclear el nombre correcto, como se puede observar en la 
    siguiente imagen; ya podemos realizar el cobro correspondiente, generalmente 
    usando la opci&oacute;n <strong>Cobro Espor&aacute;dico</strong>.</p>
</div>
<div class="texto"> 
  <p align="left"><img src="<?php echo $url;?>RegistroIngresos2.jpg"></p>
  <p align="left">Se proceder&aacute; a registrar el cobro e imprimir el comprobante 
    de forma normal.</p>
  <p align="justify"><strong>Consultar Ingresos.</strong><br>
    Finalmente, podemos hacer una consulta de ingresos por fecha o corte de caja, 
    donde visualizaremos nuestro cobro reci&eacute;n registrado.</p>
  <p align="center"><img src="<?php echo $url;?>ConsultaIngresosExterno.jpg"></p>
  <p>Importante:</p>
</div>
<ul>
  <li> 
    <div class="texto"> 
      <div align="justify">Debemos ejecutar la utiler&iacute;a <strong>CobroExternos</strong> 
        en todas las m&aacute;quinas donde se requiera realizar cobros. </div>
    </div>
  </li>
