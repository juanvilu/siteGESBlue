  <h3>Manejo de los Egresos.</h3>
  <p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
  <p align="justify">La mayor&iacute;a de instituciones educativas constantemente 
    requieren solventar algunos egresos o gastos menores, que por su escaso monto 
    no pueden ser manejados a trav&eacute;s de cheques u otros recursos financieros 
    m&aacute;s complejos.</p>
  <p align="justify">Estos egresos o gastos, entre otros pudieran ser:</p>
</div>
<ul>
  <li> 
    <div class="texto">Gastos en fotocopias.</div>
  </li>
  <li> 
    <div class="texto">Gastos en taxis.</div>
  </li>
  <li> 
    <div class="texto">Env&iacute;o de correspondencia. </div>
  </li>
  <li> 
    <div class="texto">Gastos en art&iacute;culos de impresi&oacute;n, material 
      de limpieza en peque&ntilde;as proporciones, etc.</div>
  </li>
</ul>
<p class="texto">Control Escolar GES proporciona soporte para el manejo de estos 
  Egresos deriv&aacute;ndolos de los Ingresos recibidos y del fondo inicial con 
  el que se realiza la Apertura de Caja. As&iacute;, los ingresos cobrados proporcionan 
  un mecanismo para solventar peque&ntilde;os gastos, constituy&eacute;ndose en 
  un <em>Fondo Disponible</em>.</p>
<p class="texto">Aqu&iacute; les mostraremos unos breves ejemplos que ilustran 
  esta nueva posiblidad:</p>
<div class="texto"> 
  <h4>Registro de los Egresos</h4>
  <p>Antes que todo, debemos generar nuestro cat&aacute;logo de conceptos de gasto 
    o egresos. Para lo cual utilizaremos la opci&oacute;n &quot;Configurar conceptos 
    de Cobro y Egresos&quot;, ubicada en el men&uacute; Control de Cobranza. Haga 
    &quot;clic&quot; aqu&iacute; para ver un ejemplo de la ventana de <a href="<?php echo $url;?>cfg_egresos.jpg" target="_blank"><u>Configuraci&oacute;n 
    de Conceptos de Egreso</u></a>.</p>
  <p>Se utilizar&aacute; la opci&oacute;n <strong>Registro de Egresos</strong> 
    disponible en el m&oacute;dulo de Cobranza.</p>
  <p><img class="noborder" src="<?php echo $url;?>menu_item_egresos.jpg"> </p>
  <p>Puesto que el Control de los Egresos tiene relaci&oacute;n directa con la 
    operaci&oacute;n de una de caja de cobranza, deberemos teclear la fecha y 
    la sesi&oacute;n de cobro que est&eacute; activa en ese momento.</p>
  <p><strong>Nota Importante:</strong> El registro de los egresos debe ser realizado 
    por la misma persona que est&aacute; realizando la cobranza, quien es la responsable 
    del fondo financiero en ese momento.</p>
  <p>Como se puede ver en la siguiente imagen, al momento de indicar la fecha 
    y seleccionar la sesi&oacute;n de caja se muestra un resumen de movimientos 
    con el disponible en caja al momento (<strong>Total a Entregar</strong>). 
  </p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_teclear_fecha.jpg">.</p>
  <p>El registro de un Egreso es muy simple, consiste en seleccionar el tipo de 
    gasto, la persona que recibe y cantidad que se eroga.</p>
  <p><img class="noborder" src="<?php echo $url;?>win_edit_egreso.jpg"></p>
  <p>Despu&eacute;s de registrar el egreso podr&aacute; imprimir, si as&iacute; 
    lo desea, un comprobante.</p>
  <p><img class="noborder" src="<?php echo $url;?>comprobante_egreso.jpg"></p>
  <p>En esta imagen se puede apreciar que al momento de registrar el egreso, la 
    informaci&oacute;n del recurso disponible (Total a Entregar) se modifica inmediatamente.</p>
  <p><img src="<?php echo $url;?>despues_egreso.jpg"></p>
</div>
