  <h3>Asignar nombres alternativos a las asignaturas.</h3>
  <p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
  <table width="94%" border="0" cellspacing="0" cellpadding="0">
    <tr>
      <td valign="top" class=texto><p>Para todas las instituciones educativas 
          de todo el mundo, ser&aacute; obligatorio emitir sus informes y documentos 
          probatorios en el idioma oficial de cada pa&iacute;s.</p>
        <p>No obstante, existen instituciones con educaci&oacute;n biling&uuml;e, 
          para quienes frecuentemente es necesario emitir certificados de estudios 
          o de calificaciones en un segundo idioma.</p>
        <p>Control Escolar GES permite implementar planes de estudio para dar 
          respuesta a cualquier necesidad, y por ello tambi&eacute;n cuenta con 
          la capacidad para asignar un nombre alternativo a cada asignatura.</p></td>
      <td>&nbsp;&nbsp;</td>
      <td><img class="noborder" src="<?php echo $url;?>grades_certificate.gif"></td>
    </tr>
  </table>
  <p>Si desea aplicar este procedimiento, siga estos pasos:</p>
  <p><strong>Paso 1:</strong> Ingrese a la ventana de creaci&oacute;n de Planes 
    de Estudio, a trav&eacute;s del men&uacute; Control Acad&eacute;mico y seleccione 
    el plan de estudios que Usted desee.</p>
  <p>En este ejemplo utilizaremos un plan de estudios con sistema alem&aacute;n.</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_planes.gif" width="600" height="385"></p>
  <p><strong>Paso 2: </strong>Despu&eacute;s de seleccionar el plan haga &quot;clic&quot; 
    en la opci&oacute;n [<strong>Modificar...</strong>], para ingresar a las propiedades 
    del plan.</p>
  <p>Una vez que la ventana se ha abierto, busque y active la casilla de verificaci&oacute;n 
    identificada como &quot;Definir nombres optativos de Asignaturas?&quot;.</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_props_planes.gif" width="460" height="418"></p>
  <p>Antes de salir, no olvide Registrar sus cambios usando la opci&oacute;n del 
    mismo nombre.</p>
  <p><strong>Paso 3: </strong>Al volver a la ventana anterior, haga &quot;clic&quot; 
    en la opci&oacute;n [Ver Asignaturas...].</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_planes_verasigna.gif" width="600" height="380"></p>
  <p><strong>Paso 4:</strong> En esta ventana veremos que las asignaturas est&aacute;n 
    definidas de acuerdo a un sistema en espa&ntilde;ol; podemos ver las asignaturas 
    con su nombre en espa&ntilde;ol.</p>
  <p>Para ingresar a cada asignatura haga &quot;doble clic&quot; sobre ella.</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_asigna_simple.gif" width="600" height="397"></p>
  <p><strong>Paso 5:</strong> El proceso siguiente requiere que ingresemos a cada 
    una de las asignaturas y asignar el nombre alternativo como aqu&iacute; se 
    muestra. No olvide guardar los cambios despu&eacute;s de asignar el nombre 
    de cada asignatura.</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_editasignatura.gif" width="440" height="386"></p>
  <p>Finalmente obtendremos una lista de asignaturas con el nombre optativo (p.e. 
    ESPA&Ntilde;OL / SPANISCH), como aqu&iacute; se observa:</p>
  <p><img class="noborder" src="<?php echo $url;?>ventana_asigna_optas.gif" width="600" height="397"> </p>
  <p>Adicionalmente, usando la funci&oacute;n <strong>AsignaturaNombreOptativo</strong> 
    del editor de formatos. Usted podr&aacute; obtener un informe de calificaciones 
    que incluya el nombre alternativo de la asignatura, como se muestra en este 
    ejemplo:</p>
  <p align="center"><img src="<?php echo $url;?>boleta_aleman.gif" width="500" height="378"></p>

