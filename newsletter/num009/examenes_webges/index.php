  <h3>Manejo de riesgos en la aplicaci&oacute;n de Ex&aacute;menes por computadora.</h3>
  <p align="right" class="autor"><em>Autor: Jorge E. L&oacute;pez S&aacute;nchez</em></p>
  <table width="94%" border="0" cellspacing="0" cellpadding="0">
    <tr>
      <td valign="top" class=texto>Las trampas o fraudes en los ex&aacute;menes 
        y casos de sobornos para pasar las evaluaciones, seg&uacute;n la UNESCO, 
        est&aacute;n contribuyendo a &#8220;socavar seriamente los sistemas educativos&#8221; 
        y es una pr&aacute;ctica que se extiende por el mundo.<br>
        <br>
        Las evaluaciones o ex&aacute;menes por computadora tienen innumerables 
        ventajas y pueden ayudar a contener este problema, sin embargo no lo eliminan 
        al 100%. </td>
