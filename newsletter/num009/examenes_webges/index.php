  <p><strong>Control Escolar GES</strong> apoya en solucionar esta situaci&oacute;n 
    al generar ex&aacute;menes aleatorios o por grados de dificultad, donde las 
    preguntas que reciba una persona ser&aacute;n diferentes a las que reciba 
    cualquier otra. La tecnolog&iacute;a que hemos empleado impide que un hacker 
    conozca las respuestas correctas, ya que el indicador de &quot;respuesta correcta&quot; 
    nunca viaja por la conexi&oacute;n de internet y por lo tanto no se puede 
    ver en la computadora donde se est&aacute; respondiendo el examen.</p>
  <p>A la par de implementar reglamentos y sanciones, una instituci&oacute;n educativa 
    debe identificar los siguientes factores:</p>
  <ul>
    <li><strong>El &aacute;mbito cultural</strong> <strong>orientado al enga&ntilde;o 
      o plagio </strong> <strong>en detrimento del aprendizaje</strong>. Se puede 
      evitar al fomentar la cultura de rechazo hacia las personas que realizan 
      trampas, as&iacute; como a aquellas que suplantan a otras personas al responder 
      un examen. En estas condiciones fraudulentas no se puede confiar en la validez, 
      honradez y veracidad de un examen, test o evaluaci&oacute;n por Internet.<br>
      <br>
      Si se identifican estas condiciones, no se deber&aacute;n permitir &quot;ex&aacute;menes 
      estrictos&quot; por Internet que deriven en un <strong>resultado, </strong><strong>una 
      calificaci&oacute;n o aceptaci&oacute;n</strong>. Sin embargo, si podr&aacute;n 
      aplicarse ex&aacute;menes de diagn&oacute;stico, encuestas de opini&oacute;n, 
      encuestas de servicios, evaluaci&oacute;n de profesores, etc.<br>
      <br>
      Los &quot;ex&aacute;menes estrictos&quot; podr&aacute;n ser contestados 
      por computadora, pero bajo medidas de vigilancia y en situaciones controladas 
      (p.e. en un centro de c&oacute;mputo o laboratorio). En este caso funcionan 
      bien los ex&aacute;menes aleatorios, e impedir el acceso al centro de aplicaci&oacute;n 
      portando celulares, reproductores MP3, aud&iacute;fonos de cualquier tipo, 
      entre otros aparatos.<br>
      <br>
    </li>
    <li><strong>Evitar que alguna persona diferente a un profesor tenga acceso 
      a los sistemas de c&oacute;mputo y divulgue los resultados</strong>. Se 
      evita o disminuye el riesgo, implementando fuertes medidas disciplinarias 
      en los reglamentos laborales y fomentando valores de lealtad, honradez y 
      servicio desde los directivos superiores hasta los empleados de menor rango.<br>
      <br>
      <strong>Control Escolar GES</strong> cuenta con tecnolog&iacute;a segura 
      que disminuye de forma importante el riesgo de acceso no autorizado a la 
      informaci&oacute;n, sin embargo, no podemos eliminar el riesgo de que los 
      usuarios autorizados puedan divulgar el contenido de un examen.<br>
      <br>
    </li>
    <li><strong>Que los profesores divulguen los resultados mediante el pago de 
      sobornos</strong>. Estos actos de corrupci&oacute;n nacen en la propia aula 
      de clase y tambi&eacute;n se evitan aplicando supervisi&oacute;n constante 
      y estrictas medidas disciplinarias (incluyendo medidas legales). <br>
      <br>
      Esta situaci&oacute;n es la de m&aacute;s alto riesgo ya que dif&iacute;cilmente 
      se puede predecir o identificar, pero los alumnos con m&aacute;s altos niveles 
      de honradez se convierten en grandes aliados para ayudar a identificar el 
      problema.</li>
  </ul>
  <p align="center"><img src="<?php echo $url;?>haciendo_examen.jpg"></p>
  <p>Con el an&aacute;lisis de estos factores, se podr&aacute;n valorar las condiciones 
    m&aacute;s favorables para aprovechar herramientas tecnol&oacute;gicas como 
    <strong>Control Escolar GES</strong>, que proporcionan apoyo completo para 
    sistematizar el proceso de dise&ntilde;o y aplicaci&oacute;n de ex&aacute;menes 
    por computadora.</p>
  <p>En pr&oacute;ximas entregas hablaremos de consejos para dise&ntilde;ar y 
    aplicar ex&aacute;menes, y que esta experiencia sea bien recibida por los 
    alumnos.</p>

