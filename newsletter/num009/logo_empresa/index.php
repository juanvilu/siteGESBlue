  <h3>Utilice el logotipo de su instituci&oacute;n como elemento de la ventana 
    principal. </h3>
  <p align="right" class="autor"><em>Autor: Jorge Alberto Reyes</em></p>
  <p align="justify"> A trav&eacute;s del tiempo <strong>Control Escolar GES</strong> 
    ha estado en constante evoluci&oacute;n para ofrecer mejor funcionamiento 
    a nuestros usuarios. Ellos nos han planteado infinidad de sugerencias, que 
    han sido de gran valor para robustecer y ampliar m&aacute;s el potencial de 
    los productos que ofrecemos.</p>
  <p>Una sugerencia muy constante ha sido la de personalizar la ventana principal 
    del sistema con el logotipo de la Instituci&oacute;n, con la finalidad de 
    tener una imagen representativa familiar. </p>
  <p>Esta opci&oacute;n desde hace varios meses est&aacute; disponible para todos 
    nuestros clientes y aqu&iacute; les mostramos la forma en que se realiza esta 
    configuraci&oacute;n:</p>
  <p>Nota: El logotipo que Usted observar&aacute; en estos ejemplos es propiedad 
    del Centro Escolar Lancaster.</p>
  <h4 align="justify"><strong>Procedimiento:</strong></h4>
  <p><strong>Paso 1: </strong>Seleccione el submen&uacute; Configuraci&oacute;n 
    y la opci&oacute;n <strong>[Datos de la Instituci&oacute;n&#8230;]</strong>. 
    Al centro de la ventana principal se observa el logo de Control Escolar GES 
    versi&oacute;n 4.</p>
  <p><img src="<?php echo $url;?>ventana_conlogoges.jpg" width="600" height="366"></p>
  <p><strong>Paso 2: </strong>En la ventana Configuraci&oacute;n de Datos de la 
    Instituci&oacute;n, col&oacute;quese en la secci&oacute;n <strong>Datos Adicionales</strong>. 
  <p>Nota Importante: Si a&uacute;n no lo ha hecho, en este momento podr&aacute; 
    colocar una imagen en el logotipo.
  <p><img src="<?php echo $url;?>ventana_configuracion_1.jpg" width="500" height="278">
  <p><strong>Paso 3: </strong>Por default en la lista desplegable &#8220;Imagen 
    que se colocar&aacute; como Fondo [de la ventana principal]&#8221; aparecer&aacute; 
    &quot;Logo Escolar GES&quot;. Usted podr&aacute; colocar el &quot;Logotipo&quot;, 
    &quot;Fotograf&iacute;a&quot; u &quot;Otra&quot;.
  <p>Abra esa lista desplegable y seleccione la opci&oacute;n que necesita (para 
    nuestro ejemplo utilizaremos el Logotipo).
  <p align="justify"><img src="<?php echo $url;?>ventana_configuracion_2.jpg" width="500" height="278"></p>
  <p align="justify">Cierre esta ventana, los cambios se aplicar&aacute;n autom&aacute;ticamente.</p>
  <p align="justify"><strong>Paso 4: </strong>Deber&aacute; salir del sistema 
    y al entrar nuevamente podr&aacute; observar la imagen del Logotipo ubicada 
    al centro de la ventana principal. La identificaci&oacute;n del producto Control 
    Escolar GES ser&aacute; ubicada en la esquina inferior derecha.</p>
  <p align="justify"><img src="<?php echo $url;?>ventana_conlogoescuela.jpg" width="600" height="364"></p>
  <p align="justify"> <strong>Tips para obtener un logotipo compatible con Control 
    Escolar GES.</strong></p>
  <p align="justify">En algunas ocasiones los archivos de im&aacute;genes podr&aacute;n 
    mostrar un resultado poco agradable a la vista, por diversas razones como 
    colores inadecuados, formato no soportado, baja resoluci&oacute;n, etc. Grupo 
    GES recomienda colocar un logo sencillo basado en herramientas de dibujo simples 
    como Microsoft Paint. </p>
  <p align="justify">Siga estas sencillas recomendaciones y obtendr&aacute; un 
    buen logotipo.</p>

<ol>
  <li> Prefiera los formatos BMP o JPG.</li>
    <li>Evite logotipos grandes que ser&aacute;n procesados m&aacute;s lentamente. 
      No exceda la resoluci&oacute;n de 600 p&iacute;xeles de ancho por 500 de 
      alto.</li>
    <li>Utilice un logo cuadrado o rectangular con bordes en el per&iacute;metro. 
      Este logo y cualquier imagen se mostrar&aacute; perfectamente delimitada.<br>
    <br>
    <img src="<?php echo $url;?>logo_rectangular.jpg" class="noborder" width="80" height="122"><br>
    <br>
  </li>
  <li>Si no cuenta con un logo cuadrado o rectangular, utilice el truco de &quot;efecto 
    transparencia&quot; que consiste en llenar de un color contrastante todo el 
    espacio alrededor de las l&iacute;neas, im&aacute;genes o letras del logotipo. 
    El truco consiste en que el sistema no mostrar&aacute; ese color contrastante 
    con lo cual se obtiene un efecto como el del ejemplo.<br>
    <br>
    <img src="<?php echo $url;?>logo_efecto_transparencia.jpg" width="250" height="143"> <img src="<?php echo $url;?>ventana_conlogo_transp.jpg" width="300" height="182"></li>
</ol>
<p>Lo anterior adem&aacute;s ayudar&aacute; al procesamiento de sus reportes impresos, 
  ya que un logo muy grande y pesado podr&aacute; constituir una carga importante 
  para la memoria del sistema operativo al momento de imprimir.</p>
