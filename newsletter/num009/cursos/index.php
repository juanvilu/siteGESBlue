<H3>Administración y Utilización de Cursos (&Uacute;ltima parte)</H3>

<P class=autor><EM>Autor: Maximino Celis Carbajal</EM></P>

<P>Este es el último artículo de una serie de 3, que tuvieron como objetivo dar 
  a conocer la operaci&oacute;n bajo la modalidad de Cursos con que cuenta <strong>Control 
  Escolar GES</strong>. Recomendamos a los lectores revisar los boletines <a href="http://www.grupoges.com.mx/grupoges_newsletter.php?url=cursos&numero=7&id_tipo=2&id_num=3&fecha=24/Sep/2007&titulo=cursos">7</a> 
  y <a href="http://www.grupoges.com.mx/grupoges_newsletter.php?url=cursos&numero=8&id_tipo=2&id_num=3&fecha=20/Ene/2008&titulo=cursos">8</a> 
  en los cuales se publicaron los dos primeros artículos.</P>

<P>En esta ocasión se ilustrarán otras situaciones en las cuales la opción de Cursos podrá ser de mucha ayuda, como son:</P>

<ol>
  <li>Utilización de Cursos para subdividir grupos numerosos en subgrupos pequeños para organizar las prácticas.</li>
  <li>Organizar las actividades extracurriculares.</li>
  <li>Dividir los grupos para tomar asignaturas de idiomas de acuerdo al nivel 
    (P. e. básico, intermedio, avanzado).</li>
</ol>
<H4>Caso 1. Utilización de Cursos para subdividir grupos numerosos en subgrupos pequeños para organizar las prácticas.</H4>

<P>El tamaño de los grupos varía en cada institución de acuerdo a varios factores como son, demanda, capacidad física de las aulas, recomendaciones pedagógicas, entre otras. Por lo tanto es probable que su plantel cuente con grupos de 30 a 50 alumnos o más.</P>

<P>Cuando se realizan prácticas con grupos tan numerosos resulta para el catedrático poder supervisar las actividades de cada alumno. Por lo tanto se puede tener como opción la utilización de los cursos para dividirlo en subgrupos de menor número de alumnos.</P>

<P>En la imagen siguiente podemos apreciar un grupo de 40 alumnos.</P>

<P align=center><IMG src="<?php echo $url;?>cursos3_1.jpg"></P>

<P>Para dividir el grupo de 40 alumnos, se crean dos cursos ligados a la misma asignatura. En cada uno de esos dos cursos creados existe la posibilidad de asignar dos profesores distintos. Esto normalmente sucede cuando la calendarización de los dos cursos coinciden en día y hora. De no coincidir es posible asignar a un mismo profesor para esos dos cursos.</P>

<P align=center><IMG src="<?php echo $url;?>cursos3_2.jpg"></P>

<P>Podemos apreciar que en cada curso se inscribieron a 15 alumnos para facilitar la organización de las prácticas.</P>

<H4>Caso 2. Utilización de Cursos para organizar las actividades extracurriculares.</H4>

<P>Cuando en el plan de estudios se incluyen asignaturas extracurriculares y cada alumno puede elegir a cual de ellas inscribirse, los cursos le dan la posibilidad de controlar esta situación. Recuerde que las materias de esta índole, <strong>no</strong> deben marcarse como Oficiales y <strong>si</strong> como Opcionales.</P>

<P>Se debe crear un curso por cada una de las materias extraescolares. Recuerde que si la demanda es muy alta en alguna de ellas, puede crear más de un curso referenciado a la misma asignatura.</P>

<P align=center><IMG src="<?php echo $url;?>cursos3_3.jpg"></P>

<P>Cuando asigne por primera vez el plan de estudios al alumno, las extracurriculares no se verán reflejadas en el kardex, sin embargo conforme inscriba a los alumnos en los cursos, las materias relacionadas a los cursos elegidos serán cargadas automáticamente al kardex.</P>

<H4>Caso 3. Utilización de Cursos para dividir los grupos en niveles de idiomas.</H4>

<P>La enseñanza de idiomas es muy común de todas las instituciones educativas. 
  Pero cada alumno tiene un nivel de conocimiento diferente respecto de ese idioma. 
  Por lo tanto con frecuencia es necesario subdividir a los alumnos en subgrupos 
  de acuerdo al grado de comprensión. Los cursos le permiten hacer esa subdivisión 
  creando, por ejemplo, un curso de Inglés Básico, otro de Inglés Intermedio y 
  uno más de Inglés Avanzado.</P>

<P align=center><IMG src="<?php echo $url;?>cursos3_4.jpg"></P>

<P>Con esto a cada alumno se le podrá inscribir en el curso adecuado de acuerdo a su nivel de dominio del idioma.</P>

<P>Con estas tres recomendaciones del manejo de cursos damos por concluida esta serie. Si tiene alguna duda antes de implementar el uso de cursos en su sistema, póngase en contacto con nosotros y un asesor le podrá ayudar a determinar si es necesario utilizar esta opción de acuerdo a su planteamiento y requerimientos.</P>

