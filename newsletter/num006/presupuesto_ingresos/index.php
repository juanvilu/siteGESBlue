<h3>Presupuesto de Ingresos.</h3>
<p class="autor"><em>Autor: Enrique Trujillo García</em></p>
<p>Dentro de Control Escolar GES, existe una opción bastante práctica para conocer 
  el estimado de ingresos que percibirán las instituciones educativas durante 
  un ciclo escolar, &eacute;sta se conoce como <strong>Presupuesto de Ingresos</strong> 
  y se encuentra en el menú de cobranza. </p>
<p>Para estimar los ingresos, se analizar&aacute;n las cuentas por cobrar que 
  hayan sido asignadas a los alumnos en el ciclo escolar vigente. Estas cuentas 
  por cobrar tendr&aacute;n origen en los planes de pago. </p>
  
<img border="0" src="<?php echo $url;?>menu_presupuesto.jpg" width="720" height="546"> 
<p>Existen dos variantes del presupuesto de ingresos:</p>
<ul>
  <li><strong>El presupuesto analítico simple</strong>, nos proporciona información 
    por nivel educativo o grado o grupo, acerca de las expectativas de captación 
    por cada concepto que aparece en los planes de pago.<br>
    <br>
    Este informe muestra las deducciones si los alumnos tienen becas o subsidios, 
    por ello las cifras mostradas son totalmente apegadas a la realidad. Asimismo, 
    para mantener una estimación conservadora de los ingresos, este reporte puede 
    considerar los descuentos por pronto pago.<br>
    <br>
    Durante el avance del ciclo, para efectos de vigilar la captaci&oacute;n de 
    ingresos real, con este mismo reporte obtendremos un porcentaje de la recuperación, 
    es decir, de lo que est&aacute; siendo cobrado.</li>
</ul>
<ul>
  <li><strong>El presupuesto anal&iacute;tico calendarizado</strong>, nos muestra 
    un panorama de todos los meses del ciclo escolar, respecto a los ingresos 
    esperados por cada concepto. </li>
</ul>
<p class="texto_tutorial_paso"><strong>¿Cómo obtener los presupuestos de ingresos?</strong></p>
<p align="justify">Para obtener cualquier presupuesto (simple o calendarizado), 
  primero deberemos seleccionar a cuales alumnos deseamos incorporar dentro del 
  an&aacute;lisis. En este ejemplo deseamos incorporar a todos.</p>
<p align="justify"><img src="<?php echo $url;?>seleccionar_ambito.jpg" border="0"></p>
<p align="justify">Posteriormente deber&aacute; indicar el rango de fechas para 
  realizar el an&aacute;lisis, tambi&eacute;n podrá solicitar el presupuesto espec&iacute;ficamente 
  por alg&uacute;n concepto de cobro (considerará todos a menos que Usted seleccione 
  uno en particular).</p>
