<p class="texto_tutorial_paso">Seguimiento al Presupuesto de Ingresos </p>
<p align="justify">Existe una opción llamada <strong>Seguimiento al Presupuesto</strong> 
  que sirve para dar seguimiento al presupuesto y obtener un historial de los 
  movimientos que van modificando el panorama de ingresos. Esta opci&oacute;n 
  nos informará a detalle acerca de los cambios que han alterado el presupuesto 
  original. Para ello primero deberemos bloquear o &quot;congelar&quot; las cifras 
  originales del presupuesto.<o:p> </o:p> 
<p align="justify"><strong><font size="3">Bloquear el Presupuesto</font></strong>.&nbsp; 
  Se recomienda bloquear el presupuesto una vez que se han asignado las cuentas 
  por cobrar a los alumnos y estamos conformes con las cifras de presupuesto al 
  inicio de un ciclo escolar. Posteriormente, el sistema registrar&aacute; cualquier 
  de los siguientes movimientos como modificaci&oacute;n del presupuesto original:</p>
<ul>
  <li>Bajas.</li>
  <li> Creaci&oacute;n de cuentas por cobrar a nuevos alumnos.</li>
  <li>Cambios o asignaci&oacute;n de nuevas becas o subsidios.</li>
  <li>Cambios en alg&uacute;n plan de pagos.</li>
</ul>
<p align="center"><img border="0" src="<?php echo $url;?>bloqueoalpresupuesto.jpg">
<p align="left">Despu&eacute;s de bloquear el presupuesto, el sistema guardar&aacute; 
  las cantidades originales en un lugar especial y al emitir reportes de presupuestos 
  siempre aparecer&aacute;n esas mismas cifras sin importar los movimientos posteriores 
  que pudieran afectar la informaci&oacute;n financiera. (Si desea revertir esta 
  acción se deber&aacute; desbloquear el presupuesto).
<p align="left">Posteriormente podremos utilizar el reporte de seguimiento al 
  presupuesto para conocer los movimientos que alteran los ingresos originalmente 
  estimados en el presupuesto. 
