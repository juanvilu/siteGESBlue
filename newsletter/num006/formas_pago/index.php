  <h3>Combinar formas de pago en una misma operaci&oacute;n de cobro.</h3>
    
  <p class="autor"><em>Autor:Maximino Celis Carbajal</em></p>

  <p align="justify">La cobranza es una actividad de suma importancia para las 
    instituciones educativas particulares. Desde un punto de vista estricto, de 
    esta actividad depende en gran medida la permanencia o el crecimiento de la 
    institución. </p>
  <p align="justify">Esta actividad también debe ser flexible para que los papás 
    puedan realizar sus pagos de manera ágil, pr&aacute;ctica y con variedad de 
    opciones. Pensando en ello, <em><strong>Control Escolar GES</strong></em> 
    contiene un nuevo mecanismo de forma de pago la cual permite utilizar varias 
    formas de pago en una misma operaci&oacute;n de cobranza. Le permite indicar 
    los distintos montos para cada una de ellas hasta completar la totalidad del 
    pago que se debe realizar.</p>

  <h4>¿Cómo usar las nuevas formas de pago?</h4>
  <p>Durante una operaci&oacute;n de cobro, el operador marca o agrega los diversos 
    conceptos que deber&aacute; cobrar y posteriormente proceder. </p>
  <p><img src="<?php echo $url;?>ventana_cobranza.jpg" class="noborder">.</p>

  <p>En ese momento aparecerá la ventana de <strong>Forma de pago</strong>, en 
    la parte superior de esa ventana aparecerán 5 botones de acceso r&aacute;pido 
    o &quot;atajos&quot;. Haga &quot;clic&quot; sobre estos botones u oprima la 
    tecla de funci&oacute;n necesaria (F2, F3, F4,.. etc) para seleccionar las 
    formas de pago deseadas. A la derecha de los botones mencionados, se encuentra 
    una lista con esas mismas formas de pago (se agregan haciendo &quot;clic&quot; 
    sobre el &iacute;cono adici&oacute;n [+]).</p>
  <p>Por default, siempre aparecer&aacute; una forma de pago predeterminada, en 
    este caso es <strong>Efectivo</strong> por ser la m&aacute;s utilizada.</p>
  <p><img src="<?php echo $url;?>forma_pago1.jpg"  class="noborder"></p>
  <p>Usted deber&aacute; completar la totalidad de la cantidad a cobrar para que 
    el sistema le permita <strong>Registrar el Pago</strong>. En la siguiente 
    imagen se observa una leyenda que advierte que &quot;la cantidad indicada 
    es menor a la cantidad total a cobrar&quot;.</p>

  <p><img src="<?php echo $url;?>forma_pago3.jpg"  class="noborder"></p>

  <p>Esto le dará la posibilidad de agregar otra forma de pago e introducir una 
    cantidad complementaria. La leyenda desaparecerá hasta que la suma total iguale 
    o supere el total a pagar siendo posible agregar cuantas formas de pago se 
    requiera, en este momento la opci&oacute;n <strong>Registrar el Pago</strong> 
    ya se mostrar&aacute; disponible para continuar.</p>

  <p><img src="<?php echo $url;?>forma_pago4.jpg" class="noborder"></p>

  <p>Si ha cometido una equivocaci&oacute;n es posible eliminar cualquier forma 
    de pago, usando la opci&oacute;n "<strong>Eliminar</strong>" que aparece adjunta.</p>

  <p><img src="<?php echo $url;?>forma_pago5.jpg" class="noborder"></p>
  <h4>¿Cómo determinar o configurar las formas de pago disponibles?</h4>
  <p>Control Escolar GES contiene una serie de formas de pago de uso com&uacute;n 
    en las instituciones latinoamericanas, pero Usted puede determinar sus propias 
    formas de pago o modificar el comportamiento de alguna de ellas.</p>
  <p>Esto se hace desde la sección <strong>Formas de pago</strong> dentro de la 
    ventana de <strong>Configuración de Datos de la Institución</strong>. Para 
    estos efectos solicite apoyo a su administrador de sistemas o a su asesor 
    personal.</p>
  <p><img src="<?php echo $url;?>forma_pago2.jpg"  class="noborder"></p>

  <p><a href="http://www.grupoges.com.mx">Consulta las características de 
  Control Escolar GES en su versión 4</a>. </p>

