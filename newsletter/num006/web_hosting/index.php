<h3>Grupo GES anuncia su servicio de hospedaje para web</h3>
<p class="autor"><em>Autor: Jorge Enrique L&oacute;pez S&aacute;nchez</em></p>
<p>Con el objetivo de ofrecer mejores servicios, hemos realizado importantes inversiones 
  para contar con servidores de internet propios y as&iacute; ofrecer a nuestros 
  clientes el servicio de hospedaje de aplicaciones de Internet.</p>
<p>Este servicio estar&aacute; enfocado hacia los clientes que adquieran el producto 
  <strong>Control Escolar GES para Internet</strong>, prest&aacute;ndoles as&iacute; 
  un servicio integral que incluye:</p>
<ul>
  <li>El hospedaje de la aplicaci&oacute;n Control Escolar GES para Internet.</li>
  <li>El hospedaje de la base de datos.</li>
  <li>Configuraci&oacute;n y arranque por personal especializado de Grupo GES.</li>
  <li>Cuentas de correo electr&oacute;nico institucionales (del tipo usuario@elcolegio.edu.mx).</li>
  <li>Dominios institucionales personalizados (del tipo www.elcolegio.edu.mx).</li>
  <li>Respaldos diarios de la informaci&oacute;n de Control Escolar GES.</li>
</ul>
<p>Con lo anterior, nuestros futuros clientes contar&aacute;n con la ventaja de 
  arrancar su operaci&oacute;n desde el primer d&iacute;a sin tener que recurrir 
  a otros proveedores y empresas externas de renta de espacios en internet.</p>
<p>Despu&eacute;s de una evaluaci&oacute;n cuidadosa, hemos colocado nuestros 
  servidores en un <em>Centro de Datos</em> de clase mundial en la ciudad de San 
  Jos&eacute;, California (USA). Los servidores cuentan con las siguientes caracter&iacute;sticas:</p>
<ul>
  <li>Hardware de alta calidad.</li>
  <li>Infraestructura de red y comunicaciones de &uacute;ltima tecnolog&iacute;a.</li>
  <li>Amplio ancho de banda.</li>
  <li>Garant&iacute;a de tiempo de operaci&oacute;n del 99.9%.</li>
</ul>
<p>Para m&aacute;s informaci&oacute;n respecto a este servicio p&oacute;ngase 
  en contacto con nosotros o con su distribuidor regional.</p>
