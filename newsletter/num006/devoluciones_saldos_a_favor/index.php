<h3>&iquest;C&oacute;mo realizar devoluciones de dinero?</h3>
<p align="right" class="autor"><em>Autor: Margarita Lozano Neveros</em></p>
<p align="justify">Como Usted lo sabe, Control Escolar GES est&aacute; preparado 
  para realizar el manejo de pagos por parte de los padres de familia o alumnos, 
  y esto es parte de la operaci&oacute;n cotidiana normal. Pero tambi&eacute;n 
  existen diversas circunstancias imprevistas que dan lugar a cifras pagadas superiores 
  a lo que originalmente estaba previsto para un alumno, estas cifras se denominan 
  <strong>Saldos a Favor</strong>.</p>
<p>La instituci&oacute;n se enfrenta a dos posibilidades:</p>
<ol>
  <li>Utilizar ese dinero cobrado en exceso cuando el alumno realice sus pr&oacute;ximos 
    pagos o (este tema se tratar&aacute; en el siguiente bolet&iacute;n electr&oacute;nico),</li>
  <li>Devolver el dinero de forma administrativa (p.e. En cheque).</li>
  <li>Devolver el dinero de forma directa utilizando el dinero cobrado en alguna 
    caja.</li>
</ol>
<p>Aqu&iacute; explicaremos la forma de realizar devoluciones tanto administrativas 
  como en forma directa utilizando dinero cobrado.</p>
<p class="texto_tutorial_paso"><strong>Ejemplo de la Problem&aacute;tica.</strong></p>
<p align="left"> Un alumno ser&aacute; dado de baja por razones personales el 
  d&iacute;a 30 de Junio de 2007, pero unos d&iacute;as antes hab&iacute;a realizado 
  el pago de la colegiatura del siguiente mes, servicio que ya no utilizar&aacute; 
  y por tal motivo deber&aacute; devolv&eacute;rse el dinero pagado.</p>
<p align="center"><img src="<?php echo $url;?>Edo_Cuenta.jpg" class="noborder" /></p>
<p align="left">Este alumno ha pagado $1,450.00 pesos a la colegiatura del mes 
  de Julio. </p>
<p align="center"><img src="<?php echo $url;?>Pgo_Jul.jpg" class="noborder"></p>
<p align="left" class="texto_tutorial_paso"><strong>a) Soluci&oacute;n a la problem&aacute;tica 
  con devoluci&oacute;n administrativa</strong></p>
<p align="left">El pago que se realiz&oacute; anteriormente ser&aacute; cancelado 
  y la cantidad que fue pagada ser&aacute; trasladada como Saldo a favor para 
  ser devuelta en cheque (esta &uacute;ltima operaci&oacute;n no est&aacute; cubierta 
  dentro del alcance de <strong>Control Escolar GES</strong>).</p>
<p align="left">Dentro del estado de cuenta en la modalidad Visual, se localiza 
  una opci&oacute;n llamada <strong><em>&quot;Cancelar Abono y procesar como Saldo 
  a Favor&quot;</em></strong>.<br>
  <br>
  <img src="<?php echo $url;?>CancelarAbono.jpg" class="noborder"></p>
<p align="left">Ser&aacute; necesario ubicarse sobre el concepto de pago que deber&aacute; 
  cancelarse y despu&eacute;s seleccionar la opci&oacute;n <strong>Cancelar Abono...</strong> 
  (en este caso es la colegiatura de Julio de 2007).</p>
<p align="left">Posteriormente debemos confirmar el procedimiento, como se aprecia 
  en la imagen.<br>
  <br>
  <img src="<?php echo $url;?>ConfirmacionTraslado.jpg" class="noborder"><br>
  <br>
  Y finalmente teclear la fecha de aplicaci&oacute;n del saldo a favor.</p> 
<p align="left"><img src="<?php echo $url;?>FechaAplicacion.jpg" class="noborder"><br>
  <br>
  Despu&eacute;s de hacer &#8220;clic&#8221; en la opci&oacute;n <strong>[Continuar]</strong>, 
  el sistema realizar&aacute; la aplicaci&oacute;n del saldo a favor y de inmediato 
  se mostrar&aacute; en el Estado de Cuenta.</p>
<p align="left">El pago original ser&aacute; reubicado, hacia la secci&oacute;n 
  SIN PLAN DE PAGOS y mostrar&aacute; la leyenda PAGADO CON EXCEDENTE (esto har&aacute; 
  que se genere la aplicaci&oacute;n contable por partida doble en la p&oacute;liza 
  de ingresos).</p>
<p><img src="<?php echo $url;?>EdoCta_CSaldo.jpg" ></p>
<p align="left">El sistema liberar&aacute; el concepto que hab&iacute;a sido pagado para los 
  efectos necesarios posteriores. Para este ejemplo fue la COLEGIATURA del Mes 
  de Julio de 2007.</p>
<p><img src="<?php echo $url;?>CancelarAbono2.jpg" class="noborder"></p>
<p align="left">Lo siguiente se deber&aacute; realizar utilizando la opci&oacute;n 
  <strong>Saldos a Favor, </strong>que est&aacute;<strong> </strong>ubicada en 
  el men&uacute; Cobranza.</p>
<p align="left"><img src="<?php echo $url;?>menu_saldosAFavor.jpg" class="noborder"></p>
<p align="left">Al ingresar a la ventana, nos ubicaremos en la pesta&ntilde;a 
  &#8220;Saldos a Favor originados de Excedentes&#8221; donde aparecer&aacute;n 
  los $1,450.00 pesos que hemos trasladado con el procedimiento anterior.</p>
<p><img src="<?php echo $url;?>SdoFvor_Excedentes.jpg" class="noborder"></p>
<p align="left">En este punto, puede notificarse la devoluci&oacute;n completada, 
  usando la opci&oacute;n <img src="<?php echo $url;?>BotonNotificarDevolucion.jpg" width="126" height="26"> 
  para los casos en donde la devoluci&oacute;n se procesa por cheque con un procedimiento 
  financiero ajeno al sistema Control Escolar GES.</p>
<img src="<?php echo $url;?>NotificacionDevDirecta.jpg" class="noborder">
<p align="left"><span class="texto_tutorial_paso">b) Soluci&oacute;n de la problem&aacute;tica 
  con Devoluci&oacute;n directa mediante efectivo</span></p>
<p align="left">Para que esta opci&oacute;n sea funcional, Usted deber&aacute; 
  tener la posibilidad de tomar dinero ingresado durante el d&iacute;a en alguna 
  caja y pagarlo como devoluci&oacute;n a un alumno (lo cual quedar&aacute; registrado 
  como Egreso para efecto del corte de caja). En algunos casos, por pol&iacute;ticas 
  internas, este manejo no est&aacute; permitido por lo cual deber&aacute; ser 
  evaluado cuidadosamente por el director de finanzas.</p>
<p align="left">Si nunca ha utilizado esta opci&oacute;n, deber&aacute; asegurarse 
  de que exista un concepto de Cobro especial que ser&aacute; utilizado para registrar 
  el Egreso (esto lo puede verificar desde la opci&oacute;n Cat&aacute;logo de 
  Conceptos de Cobro y Egresos del men&uacute; Cobranza.)</p>
<p align="left"><img src="<?php echo $url;?>configurarConcepto.jpg" class="noborder"></p>
<p align="left">As&iacute; se observa un concepto de egreso &quot;especial&quot; 
  para cancelar saldos a favor (mediante devoluciones):<br>
  <br>
  <img src="<?php echo $url;?>Config_Concepto.jpg" class="noborder"></p>
<p align="left">El registro del egreso se realiza desde la opci&oacute;n del mismo 
  nombre, ubicada en el men&uacute; Cobranza.</p>
<p align="left"><img src="<?php echo $url;?>menu_Egresos.jpg" > 
</p>
<p align="left">Aparecer&aacute; la ventana de Egresos donde deber&aacute; introducir 
  datos relativos a la fecha de registro del egreso y la caja de donde se tomar&aacute; 
  el dinero que ser&aacute; devuelto al alumno.</p>
<p align="left"><img src="<?php echo $url;?>ControlRegEgreso.jpg" class="noborder"></p>
<p align="left">Haga &quot;clic&quot; en la opci&oacute;n <strong>Registrar Egreso</strong>, 
  y as&iacute; finalmente podr&aacute; devolver el dinero al alumno. Observe que 
  deber&aacute; utilizar el concepto de egreso &quot;especial&quot; y seleccionar 
  el alumno en cuesti&oacute;n.<br>
  <br>
  <img src="<?php echo $url;?>RegistroEgreso.jpg" class="noborder"><br>
  <br>
  Con esto, al ingresar nuevamente al estado de cuenta ya no se observar&aacute; 
  el saldo a favor y se mostrar&aacute; el movimiento de DEVOLUCION.</p>
<p><img src="<?php echo $url;?>EdoCtaSinSaldoAFavor.jpg" class="noborder"></p>
<p><strong>Aclaraciones Administrativas y Contables:</strong></p>
<ul>
  <li> 
    <div align="justify" class="texto">El recibo o recibos de pago realizado, 
      <strong>no se cancelar&aacute;</strong> para evitar alterar los cortes de 
      caja y la precontabilizaci&oacute;n de los ingresos.<em><strong> Permanece 
      intacto.</strong></em></div>
  </li>
  <li> 
    <div align="justify" class="texto"> El concepto de pago ser&aacute; reubicado 
      a una secci&oacute;n SIN PLAN DE PAGOS.</div>
  </li>
  <li> 
    <div align="justify" class="texto"> La cantidad que hab&iacute;a sido pagada, 
      se transferir&aacute; autom&aacute;ticamente a Saldos a Favor.<br>
    </div>
  </li>
</ul>
