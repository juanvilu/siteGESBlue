<h3>Grupo GES anuncia nuevos tel&eacute;fonos de atenci&oacute;n a clientes</h3>
<p class="autor"><em>Autor: Jorge Enrique L&oacute;pez S&aacute;nchez</em></p>
<p>Comprometidos con el objetivo de ofrecer mejores servicios, ahora decidimos 
  ampliar los medios para estrechar los lazos de contacto entre los clientes y 
  nuestro staff de ventas y soporte. En esta ocasi&oacute;n, Grupo GES ha adquirido 
  l&iacute;neas telef&oacute;nicas para servir localmente a las &aacute;reas metropolitanas 
  de Monterrey, Guadalajara y la Ciudad de M&eacute;xico.</p>
<p>Ahora los clientes de esas importantes ciudades podr&aacute;n mantener contacto 
  telef&oacute;nico con nuestras oficinas ubicadas en la ciudad de Cuernavaca, 
  con el costo de una llamada local.</p>
<p>Los n&uacute;meros telef&oacute;nicos son:</p>
<ul>
  <li>Para Monterrey: (81) 11 070866.</li>
  <li>Para Guadalajara: (33) 10 311634.</li>
  <li>Para la Ciudad de M&eacute;xico: (55) 12 047520.</li>
</ul>
<p>Esto forma parte de una serie de nuevos servicios y valores agregados que estaremos 
  anunciando en los pr&oacute;ximos meses, como parte de una estrategia para cobertura 
  integral de los mercados de esas ciudades y zonas aleda&ntilde;as. </p>
<p>Pr&oacute;ximamente anunciaremos este servicio para otras ciudades del interior 
  de la Rep&uacute;blica.<br>
</p>
