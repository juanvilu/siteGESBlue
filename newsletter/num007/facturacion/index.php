  <h4>Criterios de facturaci&oacute;n</h4>

  <p><strong>Control Escolar GES </strong> permite generar facturas utilizando 
    para ello los cobros que han sido registrados previamente. Cada factura puede 
    considerar uno o varios cobros, e incluir&aacute; todos los conceptos que 
    hayan sido recibidos en tales cobros. Para mayor detalle observe el siguiente 
    diagrama:</p>
  <p align="center"><img class="noborder" src="<?php echo $url;?>grafico_facturacion.jpg"></p>
  
  <p>Como se observa en el diagrama anterior, las facturas se preparan generalmente a petición de los clientes, sin embargo 
  las instituciones pueden establecer un procedimiento cotidiano de emisión de facturas sin necesidad de que exista una petición expresa.</p>

  <h4>Procedimiento para elaborar una factura</h4>

  <p><strong>Paso 1:</strong> Ingrese a la opci&oacute;n &quot;Facturaci&oacute;n&quot;, 
    ubicada en el submen&uacute; Cobranza.</p>
  <p><img src="<?php echo $url;?>menu_item_facturacion.jpg"></p>
  <p>Al seleccionar esta opci&oacute;n, obtendremos la ventana de Facturaci&oacute;n:</p>
  <p align="left"><img src="<?php echo $url;?>ventana_facturacion.jpg" /></p>
  <p align="left"><strong>Paso 2:</strong> Los primeros datos a ingresar son la 
    fecha de facturaci&oacute;n y el n&uacute;mero de factura, por lo tanto, de 
    acuerdo a la disponibilidad f&iacute;sica de papel de facturaci&oacute;n indique 
    la fecha y la factura disponible.</p>
  <p align="left"><img src="<?php echo $url;?>seccion_facturacion_teclearfechaynumero.jpg" /></p>
  <p align="left">Si el usuario introduce un n&uacute;mero de factura y fecha 
    que ya hayan sido utilizados con anterioridad, el sistema mostrar&aacute; 
    un mensaje de advertencia.</p>
  <p align="left"><strong>Paso 3:</strong> Ahora deber&aacute; indicar la forma 
    en que desea generar la factura (por Alumno o por Familia).</p>
  <p>Al seleccionar por alumno, solo podr&aacute; incorporar a la factura recibos 
    de ese alumno.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_clickporalumno.jpg" /> 
  </p>
  <p>Cuando se genera un recibo por familia, se podr&aacute;n incorporar recibos 
    de todos los alumnos que pertenezcan a esa familia.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_clickporfamilia.jpg" /></p>
  <p><strong>Paso 4</strong>: Seleccione el alumno o familia haciendo &#8220;clic&#8221; 
    en las opciones de igual nombre.</p>
  <p>En la siguiente imagen se tiene un ejemplo del aspecto de la informaci&oacute;n 
    al seleccionar un alumno.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_ejemploxalumno.jpg" /></p>
  <p>En la parte inferior se muestran los recibos pendientes de facturar del alumno 
    o de la familia.</p>
  <p><strong>Paso 5:</strong> En la parte media de la ventana, el sistema muestra 
    los datos de facturaci&oacute;n que est&eacute;n registrados en la Ficha General 
    del Alumno.</p>
  <p>En este momento, Usted podr&aacute; realizar cualquier cambio a estos datos 
    y tendr&aacute; la facilidad de actualizar los datos originales de la ficha 
    del alumno.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_datosfacturacion.jpg" /></p>
  <p>Si realiz&oacute; alg&uacute;n cambio, y desea que estos cambios permanezcan 
    en la ficha del alumno, marque la casilla &quot;Aplicar esta informaci&oacute;n 
    a los datos...&quot;.</p>
  <p><strong>Paso 6:</strong> Ahora seleccione los recibos que desee incorporar 
    en la factura.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_marcarrecibos.jpg" />g</p>
  <p>Observe que en la parte inferior se tiene el dato del total a facturar, que 
    es la suma de los recibos que est&aacute;n siendo marcados.</p>
  <p><strong>Paso 7: </strong>Para guardar la factura haga &#8220;clic&#8221; 
    en la opci&oacute;n &quot;Registrar e Imprimir Factura&quot;.</p>
  <p><img class="noborder" src="<?php echo $url;?>seccion_facturacion_clickregistrar.jpg" /></p>
  <p>Se realizar&aacute; una pregunta para confirmar y posteriormente el sistema 
    enviar&aacute; el siguiente aviso:</p>
  <p><img class="noborder" src="<?php echo $url;?>confirmar_facturacion_imprimir.jpg" /></p>
  <p>Aqu&iacute; mostramos el formato predeterminado de facturaci&oacute;n, sin 
    embargo como es sabido, Control Escolar GES le da la facilidad de adaptar 
    el formato de impresi&oacute;n de acuerdo a sus gustos y o necesidades (incluyendo 
    la adaptaci&oacute;n a formatos previamente impresos).</p>
  <p><img class="noborder" src="<?php echo $url;?>formato_predeterminado.jpg" /><br>
  </p>
