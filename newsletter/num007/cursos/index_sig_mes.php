  <H3>Administración y utilización de Cursos (N&uacute;mero 1 de 3)</H3>
  <P class=autor><EM>Autor: Maximino Celis Carbajal</EM></P>
  <P>En el diario operar de las instituciones educativas es común enfrentarse 
    a la necesidad de controlar las clases de los alumnos bajo variados esquemas, 
    en los cuales coinciden diversas situaciones como:</P>
  <UL>
    <LI> Unir alumnos de distintos grados y especialidades en materias en común.</LI>
    <LI> Controlar horarios sin tener una estructura fija de sesiones r&iacute;gidas.</LI>
    <LI> Permitir y controlar la modalidad de elección libre de materias por cada 
      alumno.</LI>
    <LI>Organizar a los alumnos en &quot;talleres&quot; o &quot;pr&aacute;cticas&quot; 
      de asignaturas como Educación Artistica y Educación Tecnológica.</LI>
    <LI>Administrar las actividades extracurriculares.</LI>
    <LI>Organizar grupos para tomar asignaturas de idiomas de acuerdo a niveles 
      de preparaci&oacute;n (p.e. Básico, Intermedio o Avanzado).</LI>
  </UL>
  <p>Control Escolar GES ofrece soporte a todas estas situaciones a trav&eacute;s 
    de la administraci&oacute;n de clases por <strong>Cursos</strong>. Se recomienda 
    a todos los lectores de este primer número que estén al pendiente de la serie 
    completa para así conocer y evaluar la gama completa de posibilidades que 
    esta potente herramienta les brinda.</p>
</DIV>
<DIV class=texto align="justify"> 
  <P>&lt;insetar gr&aacute;fica&gt;</P>
  <h4>&iquest; Administrar las clases por Cursos o usar la administraci&oacute;n 
    tradicional por Grupos ?</h4>
  <P>Para diferenciar a los grupos tradicionales y a los cursos. Podemos definir 
    lo siguiente:</P>
  <UL>
    <LI> 
      <DIV class=texto>Un grupo tradicional estar&aacute; formado por un conjunto 
        de alumnos de un nivel o especialidad y grado específico.</DIV>
    <LI> 
      <DIV class=texto>Un curso est&aacute; formado para tomar una asignatura 
        y puede estar integrado por alumnos de varias especialidades y grados.</DIV>
    </LI>
  </UL>
  <P>Para utilizar la opción <strong>Cursos</strong> y obtener su máximo beneficio, 
    los elementos que deben estar previamente configurados son:</P>
  <UL>
    <LI> 
      <DIV class=texto>Los planes de estudio.</DIV>
    <LI> 
      <DIV class=texto>Los alumnos deben tener asignado su plan de estudio.</DIV>
    <LI> 
      <DIV class=texto>Tener ubicados a los alumnos en grupos tradicionales.</DIV>
    <LI> 
      <DIV class=texto>Tener dados de alta los profesores.</DIV>
    <LI> 
      <DIV class=texto>Tener dadas de alta las aulas.</DIV>
    </LI>
  </UL>
  <p>En este primer número de la serie dedicada al manejo de los cursos, nos ocuparemos 
    de las necesidades que principalmente se presentan en las instituciones de 
    nivel superior.</p>
  <H4>Creación del Catálogo de Cursos</H4>
  <P>Ingrese en el menú Control Académico y seleccione la opción llamada "Cursos".</P>
  <P align=center><IMG src="<?php echo $url;?>cursos1_2.jpg"></P>
  <P>Al seleccionar la opci&oacute;n se desplegar&aacute; un submenú con todas 
    las posibilidades para manipular la información organizada por cursos. De 
    este menú elija la primera opción llamada Catálogo de Cursos.</P>
  <P>En la parte superior de la ventana que aparezca podremos ver el ciclo escolar 
    con el cual se pretende trabajar e incluso seleccionar ciclos escolares pasados.</P>
  <P align=center><IMG src="<?php echo $url;?>cursos1_4.jpg"></P>
  <P>En el borde inferior encontrará las 6 opciones principales para trabajar 
    con el catálogo de cursos.</P>
  <P align=center><IMG src="<?php echo $url;?>cursos1_5.jpg"></P>
  <P>Para crear un nuevo curso haga clic en la opción &quot;Agregar&quot;, con 
    lo cual se desplegará una ventana como esta:</P>
  <P><strong>&lt;capturar una imagen de la ventana de creaci&oacute;n de cursos&gt;</strong></P>
  <P>Aqu&iacute; deber&aacute; ingresar la siguiente información:</P>

  <ul>
    <li> <strong>Codigo del Curso - </strong> Identificador del curso. La nomenclatura 
      a utilizar queda a total libertad, pero se recomienda que esté relacionada 
      con el código de la asignatura con la cual se va a ligar el curso.<br><br></li>
    <li><strong>Descripción del Curso - </strong> Nombre largo con el cual desea 
      describir el curso.<br><br></li>
    <li><strong>Profesor que impartirá la clase (Profesor Titular) - </strong> 
      Al hacer clic en el icono del signo de interrogación (?) se desplegará una 
      pantalla en la cual debe teclear el nombre o apellido del profesor deseado, 
      el sistema mostrará la lista de profesores dados de alta en el sistema que 
      cumplan con el texto de búsqueda. De ellos seleccione el profesor deseado 
      con doble clic sobre el nombre elegido.<br><br><IMG src="<?php echo $url;?>cursos1_7.jpg"><br><br></li>
	<li><P><strong>Profesor suplente</strong> – Si existe algún profesor adjunto 
        o suplente siga la misma secuencia que se describió para la elección 
        del Profesor Titular.</P>
    </li>  
	<li><P><strong>Asignatura que se impartirá</strong> – Debe elegir la asignatura 
        que va a ser impartida en este curso. Haga clic en el icono del signo de interrogación (?) y podrá elegir 
        un plan de estudios y la materia deseada. En este momento el sistema validará si la materia elegida esta en el 
		perfil académico del profesor y de no estarlo, mostrará un mensaje de advertencia.</P>
      <IMG src="<?php echo $url;?>cursos1_8.jpg"><br><br></li>
	<li><P><strong>Cupo Máximo de Alumnos</strong> – Número limite de alumnos deseados por curso.</P>
      <P align=center><IMG src="<?php echo $url;?>cursos1_8.jpg"></P></li>
	<li><P><strong>Fecha de inicio –</strong> Por lo general esta fecha coincide con la fecha de inicio del ciclo escolar pero puede ser distinta.</P>
	</li>
	<li>
      <P><strong>Fecha de conclusión </strong>– Por lo general esta fecha coincide 
        con la fecha de fin del ciclo escolar pero también puede ser distinta.</P>
	</li>
	<li> 
      <P><strong>Número de sesiones</strong> – El número de sesiones le permitirá 
        obtener el porcentaje de inasistencias.</P>
	</li>			
  </ul>

  <P>Con los datos anteriores el curso estará habilitado únicamente para recibir 
    alumnos del plan de estudios y asignatura seleccionada, pero si la finalidad 
    es poder combinar alumnos de distintas carreras en el mismo curso, deberemos 
    trasladarnos a la segunda pestaña para definir “Asignaturas Suplementarias”.</P>

  <P>En esta ventana deber&aacute; utilizar la opci&oacute;n <strong>Agregar [+]</strong> 
    y seleccionar el plan de estudios y la asignatura deseada. Esto ser&aacute; 
    necesario para todas las asignaturas que deseen integrarse en el mismo curso.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_9.jpg"></P>

  <P>La institución deber&aacute; asegurarse que el contenido académico de todas 
    las asignaturas sea compatible, pese a que los c&oacute;digos de las asignaturas 
    pudieran ser diferentes.</P>

<P>Para guardar el curso  haga "clic" en la opción [Guardar].</P>

<P>Una vez agregado el curso, éste se visualizará en la pantalla del catálogo de cursos y el curso estará listo para aceptar inscripciones de alumnos que tengan asignado cualquiera de los planes de estudios de las asignaturas principal o suplementarias.</P>

<P>Estos pasos deberán repetirse por cada uno de los cursos que se desean tener en el catálogo. En el caso de las universidades que optan por habilitar toda su oferta académica de este modo, deberá repetirse el proceso por cada asignatura que se abra.</P>


  <H4>Controlar horarios sin tener una estructura fija de sesiones.</H4>

  <P>Hasta el momento solo se ha explicado la creación de los cursos. Pero una vez 
    hecha esta labor, es necesario indicar el lugar y tiempo en que se impartirá 
    cada uno de ellos. Esto lo podemos determinar en el mismo Catálogo de Cursos 
    con la libertad de crear los intervalos de tiempo de acuerdo a las necesidades 
    de la institución y sin regirse por una estructura fija de tiempos.</P>

  <P>Para proceder con la <strong>calendarización</strong>, seleccione con un 
    &quot;clic&quot; el curso con el que desea trabajar, luego use la opción [<strong>Calendarizar</strong>] 
    que se encuentra en el borde inferior derecho de la ventana.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_10.jpg" class="noborder"></P>

  <P>En ese momento se desplegará una ventana con la identificaci&oacute;n del 
    curso, tambi&eacute;n se ver&aacute; una tabla en la cual se encuentran los 
    d&iacute;as de la semana. Dado que en esta funcionalidad no estamos sujetos 
    a horarios r&iacute;gidos, Usted podr&aacute; indicar cualquier sesi&oacute;n 
    en cualquier d&iacute;a.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_11.jpg" class="noborder"></P>

  <P>Haga &quot;clic&quot; en el recuadro del día deseado (P. e. Martes). Con 
    ello se desplegará una ventana llamada “Agregar Horario de Clase”.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_12.jpg" class="noborder"></P>

<P>Lo primero que deberá indicar es la hora inicial y la hora final del curso y posteriormente tendrá que elegir una aula. El sistema en base a esta información validará varias situaciones como son:</P>

<UL>
  <LI>
  <DIV class=texto>Que el rango declarado entre la hora de inicio y la hora final estén dentro de la disponibilidad del profesor.</DIV>
  <LI>
  <DIV class=texto>Que el profesor no tenga ya asignada otro curso en ese rango de tiempo.</DIV>
  <LI>
  <DIV class=texto>Que el aula no este ocupada por otro curso en ese rango de tiempo. Si desea visualizar con claridad las aulas ocupadas y las disponibles marque la casilla “Identificar automáticamente las aulas no disponibles”.</DIV></LI></UL>

<P align=center><IMG src="<?php echo $url;?>cursos1_13.jpg" class="noborder"></P>

<P>Para dejar indicado el horario y el aula en el día seleccionado, solo haga clic en la opción Agregar.</P>

<P>Esto lo regresará a la ventana de la Calendarización y en la cual pude repetir este proceso para los demás días en los que se impartirá el curso. Para regresar a la pantalla del catálogo de cursos, solo haga clic en la opción Salir de esta opción.</P>

<P>En la última columna del listado del catálogo de cursos el sistema automáticamente marcará los cursos que ya hayan sido calendarizados.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_14.jpg"></P>

<P>Una vez que se tiene creado el catálogo de cursos, es probable que necesite obtener un listado de todos los cursos ofertados. Para ello solo basta con hacer clic en la opción Lista de Cursos que se encuentra en el borde inferior derecho de la pantalla.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_5.jpg"></P>

<H4>Cursos para: "Permitir y controlar la modalidad de elección libre de materias por ciclo de cada alumno".</H4>

<P>Los cursos están diseñados para permitir inscribir en ellos a cualquier alumno que en su plan de estudios tenga la asignatura principal ligada al curso, o alguna de las asignaturas suplementarias declaradas. Esto sin importar si la materia es de un grado distinto al grado del alumno. Por lo tanto si se requiere inscribir a un alumno que en su ficha indique que es de 5º semestre, a un curso ligado a una asignatura de 7º semestre, el sistema si lo permitirá.</P>

<P>Para realizar estas inscripciones, ingrese en el menú Control Académico y seleccione la ultima opción llamada Cursos. Al seleccionar esa opción, se despliega un menú del lado derecho, el cual muestra todas las opciones para manipular la información organizada por cursos. De este menú elija la segunda opción llamada Inscripción a Cursos.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_15.jpg"></P>

<P>En la pantalla que se despliega encontrará, en la parte superior, el ciclo escolar en el cual se esta trabajando. Debajo de esta opción encontrará la opción para elegir el curso deseado. Para seleccionarlo haga clic en el icono con el signo de interrogación (?) del cual se desplegará el listado de cursos existentes. Si conoce el código del curso también puede teclearlo.Para accesar a la inscripción de alumnos en un curso, también es posible hacerlo dando doble clic sobre el cursos deseado en el catálogo de cursos.</P>

<P>Una vez seleccionado el curso deseado, utilice el botón Inscripción que se encuentra en la barra de opciones del lado derecho de la pantalla. Al hacer clic en esta opción, se desplegará una ventana de dialogo en la cual debe buscar al alumno que se desea inscribir ya sea por nombre empezando por el apellido paterno, o por matrícula. Cuando se selecciona al alumno, el sistema hace las siguientes validaciones:</P>

<UL>
  <LI>
  <DIV class=texto>Que el alumno tenga en su plan de estudios la asignatura principal o alguna de las suplementarias con la cual esta ligado el curso. De no ser así el sistema no permitirá la inscripción.</DIV>
  <LI>
  <DIV class=texto>Si el sistema detecta que el alumno ya curso y aprobó en un ciclo pasado este curso, tampoco permitirá la inscripción.</DIV>
  <LI>
  <DIV class=texto>Si el sistema detecta que el alumno ya curso pero no aprobó el curso en un ciclo pasado, antes de enlistarlo preguntará si se desea inscribir como un recursamiento.</DIV>
  <LI>
  <DIV class=texto>Que el horario asignado en la calendarización del curso en el cual se pretende inscribir al alumno, no se traslapa con el horario de algún otro curso en el cual ya se encuentra inscrito.</DIV>
  <LI>
  <DIV class=texto>Que el alumno no haya rebasado el límite de inscripción a cursos permitidos por la institución en un mismo ciclo escolar.</DIV>
  <LI>
  <DIV class=texto>Que la asignatura ligada al curso no esté seriada con alguna asignatura no aprobada aún por el alumno.</DIV>
  <LI>
  <DIV class=texto>Que no se haya rebasado el cupo máximo del curso.</DIV></LI></UL>

<H4>Los beneficios del manejo de los cursos en las instituciones de nivel superior.</H4>

<P>Recapitulando algunos beneficios ya mencionados en este primer numero de la serie dedicada a administración y utilización de Cursos, y resaltando algunos otros beneficios que nos da la organización por cursos, podemos mencionar los siguientes.</P>

<P>Cursos con alumnos de distintos grados y niveles educativos. En la imagen inferior se puede ver que en un curso hay enlistados alumnos de distintas licenciaturas y grados.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_16.jpg"></P>

<P>Alumnos con libertar de elegir las asignaturas a cursar por ciclo. Si se ingresa en la opción Cursos por Alumno del menú que se despliega al seleccionar la opción Cursos, visualizará la los cuales el alumno se le ha inscrito. Esto debido a la libertad que ofrece el sistema en los cursos de no restringir la inscripción solo a asignaturas del grado que esta registrado en la ficha del alumno.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_17.jpg"></P>

<P>Horarios personalizados para cada alumno. En esta misma opción se pude visualizar el horario del alumno que se va creando de acuerdo a la calendarización de cada cursos elegido. Por lo tanto cada alumno puede tener un horario distinto.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_18.jpg"></P>

<P>Opciones de horario y catedrático para los alumnos. En el catálogo de cursos se pueden crear dos o mas cursos ligados a la misma asignatura principal y mismas asignaturas suplementarias y elegir un profesor distinto en cada uno de ellos o calendarizaciones distintas. Por lo tanto los alumnos tienen la opción de inscribirse en un curso con el profesor deseado o en el horario que más se acomode a sus necesidades.</P>

<P align=center><IMG src="<?php echo $url;?>cursos1_19.jpg"></P>

<P>Listados unificados y captura de calificaciones unificada para los profesores que imparten los cursos. Si en el curso se encuentran alumnos de distintas licenciaturas y grados, al profesor se le puede entregar una lista la cual contenga a todos los alumnos sin importar grado y carrera. Esto facilita que el profesor haga el pase de lista o la captura de calificaciones con un solo documento. El único requisito para que la captura de calificaciones sea por curso, es que los métodos de evaluación de todas las asignaturas que están involucradas en el curso sean iguales.</P>

<P>Recuerde estar al pendiente de los próximos boletines en los cuales se le dará continuación a esta herramienta de organización y agrupación de alumnos para tener conocimiento amplio de todo lo que usted puede lograr con ella.</P>

