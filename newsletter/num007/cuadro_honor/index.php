<h3>Obtenci&oacute;n del Cuadro de Honor</h3>
<p class="autor"><em>Autor: Enrique Trujillo García</em> </p>
<p>Para la gran mayoría de las instituciones educativas, es muy importante que 
  los alumnos y padres de familia se sientan motivados por el desempeño académico 
  que existe al interior de la institución y por ello implementan mecanismos de 
  comunicación sobre el comportamiento de los mismos. </p>
<p>Uno de ellos es precisamente el Cuadro de Honor, con el cual, se dan a conocer 
  los mejores promedios de los alumnos y por consecuencia un estímulo a los alumnos 
  que cuentan con los mejores promedios ya sea por periodos o al finalizar el 
  ciclo escolar. Enseguida mostraremos con el siguiente ejemplo, como con Control 
  Escolar GES, se obtiene este proceso:</p>

<p> <strong>Paso 1:</strong> Dentro del submenú Control Académico, seleccionamos 
  la opción siguiente:&nbsp; </p>
<p><img border="0" src="<?php echo $url;?>cuadro_honor.jpg"><br>
 
<p align="justify"><strong>Paso 2:</strong> Posteriormente seleccionaremos el Nivel Educativo 
  y aspectos (calificaciones) con los que deseamos obtener el resultado del 
  cuadro de honor.</p>
<p align="center"><img border="0" src="<?php echo $url;?>aspectos_cuadrohonor.jpg" ></p>
<strong>Paso 3:</strong> Se pueden seleccionar uno o varios aspectos de la evaluación 
asi como aplicar filtros con las condiciones que establezcamos para el resultado 
del mismo, las cuales van desde la calificación mínima requerida, que nos muestre 
solo a los alumnos y las calificaciones más altas, que se divida a los alumnos 
por el grupo en el que se encuentran cursando el ciclo escolar y si es necesario, 
excluir aquellos alumnos que aunque tengan un promedio sobresaliente, hayan reprobado 
algun periodo de evaluación. 
<p><img border="0" src="<?php echo $url;?>filtros_cuadrohonor.jpg"></p>

<p><strong>Paso 4:</strong> Despues de elegir las condiciones anteriores, seleccionamos 
  [<strong>Obtener el Cuadro de Honor</strong>], el cual nos mostrará el resultado 
  del mismo si es que las condiciones o filtros marcados anteriormente son válidos 
  para su obtención.&nbsp; 
<p><img border="0" src="<?php echo $url;?>resultado_cuadrohonor.jpg"></p>
  
<p><strong>Paso 5:</strong> Enseguida veremos los distintos reportes que podemos procesar 
del mismo como puede ser una lista simple con promedios, un cuadro de honor para 
publicar en algún sitio físico de la escuela o bien, generar un diploma para cada 
alumno que destacó dentro de la institución por sus promedios. Ejemplos: </p>
