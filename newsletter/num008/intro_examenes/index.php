<p> Este mecanismo es una propuesta para modernizar el esquema de aplicaci&oacute;n 
  de ex&aacute;menes que hoy en d&iacute;a a&uacute;n consiste en contestar preguntas 
  en una hoja de papel.</p>
  <p>El dise&ntilde;o del examen se realiza de manera muy sencilla incorporando 
    una bater&iacute;a de preguntas y respuestas. El examen ser&aacute; activado 
    en cualquier momento y podr&aacute; ser respondido por una o m&aacute;s personas.</p>
  <p><img src="<?php echo $url;?>exams_screen1.gif" width="480" height="335"></p>
  <p>Una vez concluida la aplicaci&oacute;n de un examen, autom&aacute;ticamente 
    se emitir&aacute; la calificaci&oacute;n y la persona tendr&aacute; la posibilidad 
    de recibir sus resultados por correo electr&oacute;nico.</p>
  <p>Como se observa, se trata de la evoluci&oacute;n hac&iacute;a un esquema 
    seguro, &aacute;gil y confiable de evaluaci&oacute;n por computadora. Adem&aacute;s, 
    este mismo mecanismo permitir&aacute; la aplicaci&oacute;n de evaluaciones 
    a profesores y empleados, as&iacute; como tambi&eacute;n hace posible la difusi&oacute;n 
    de encuestas hacia los padres de familia y p&uacute;blico en general.</p>
