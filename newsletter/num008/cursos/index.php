<P>En el número anterior del boletín, se public&oacute; la primera parte de este 
  tema dedicado a la Administración y Utilización de Cursos usando <strong>Control 
  Escolar GES</strong>. En ella se explicaron los pasos para crear los cursos, 
  por lo tanto en esta continuación del tema, no se abundará en ello. También 
  se ejemplificó su uso en instituciones de educación superior.</P>

<P>Para iniciar, mencionaremos los beneficios que obtendremos al manejar la oferta 
  acad&eacute;mica por cursos:</P>

<P><strong>1. Cursos con alumnos que provienen de distintos grados y niveles educativos</strong>. 
  En la imagen se puede ver que un curso puede contener alumnos de distintas carreras.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_1.jpg"></P>

<P><strong>2. Alumnos con libertad de elegir las asignaturas que cursar&aacute;n</strong>. 
  Podemos ver que un alumno de tercer semestre est&aacute; inscrito en diferentes 
  asignaturas, sin importar que el semestre de la asignatura sea distinto.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_2.jpg"></P>

<P><strong>3. Horarios personalizados para cada alumno</strong>. Despu&eacute;s 
  de inscribir al alumno en los cursos de su preferencia, puede visualizar el 
  horario que se va creando de acuerdo a las fechas de cada curso elegido.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_3.jpg"></P>

<P><strong>4. Diversas opciones de horario y profesor. </strong>Para que los alumnos 
  tengan la posibilidad de cursar una asignatura con el profesor deseado o en 
  el horario que mejor se adapte a sus necesidades.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_4.jpg"></P>

<P><strong>5. Listados y captura de calificaciones unificada para los profesores</strong>. 
  Si en el curso se encuentran alumnos de distintas licenciaturas y grados, al 
  profesor se le puede entregar una lista la cual contenga a todos los alumnos 
  sin importar grado y carrera. El único requisito para que la captura de calificaciones 
  sea por curso, es que los métodos de evaluación de todas las asignaturas que 
  están involucradas en el curso sean iguales.</P>

<P>Para mayor referencia recuerde que puede consultar el <a class=ligas_hilite href="http://www.grupoges.com.mx/grupoges_newsletter.php?numero=7">boletín 
  anterior</a>.</P>

<P>En este número explicaremos la utilidad de los cursos en Preparatoria y Secundaria.</P>

<H4>a) Utilización de Cursos en Preparatoria</H4>

<P>En el nivel medio superior los alumnos deben ir perfilándose para la elección 
  de su carrera profesional. Por lo general en el último año algunas de las asignaturas 
  que cursar&aacute;n están relacionadas con su futura área profesional. Para 
  abordar esta situación desde la opción de cursos se pueden seguir estos pasos:</P>

<P><strong>Paso 1</strong>. Se debe contar con un plan de estudios con las materias 
  de Área definidas como Opcionales.<br>
  <br>
  Como se muestra en la imagen siguiente, hay asignaturas comunes y se deben definir 
  como OFICIALES; existen otras relacionadas con las distintas áreas de especialidad 
  las cuales se marcar&aacute;n como OFICIALES y OPCIONALES.</P>

<P>

<P align=center><IMG src="<?php echo $url;?>cursos2_5.jpg"></P>

<P>Las asignaturas opcionales no se integrar&aacute;n a la lista de asignaturas 
  de los alumnos de nuevo ingreso durante el proceso de Creación de Kardex. Esto 
  tiene sentido puesto que aún no se sabe cuales de ellas podr&aacute; elegir 
  en cada ciclo escolar.</P>

<P><strong>Paso 2</strong>. Crear el cat&aacute;logo de cursos, ligando a cada 
  curso con una asignatura opcional. Teniendo como resultado un curso por cada 
  asignatura ofertada durante el semestre.</P>

<P>Para crear cursos siga los pasos descritos en el número anterior del boletín. 
</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_6.jpg"></P>

<P><strong>Paso 3</strong>. Inscribir alumnos en esos cursos.</P>

<P>Cuando enlistemos a un alumno en alguno de los cursos, el sistema simultáneamente 
  enlistar&aacute; al alumno y agrega la asignatura en su k&aacute;rdex. Previamente, 
  el sistema verificará si ya ha sido inscrito a la asignatura en cuyo caso rechazar&aacute; 
  la solicitud de inscripci&oacute;n.</P>

<P>En la imagen siguiente se puede apreciar el Kardex de una alumna antes de ser 
  inscrita en un curso con asignatura de Derecho. Como se puede apreciar no aparece 
  la asignatura "Derecho".</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_7.jpg"></P>

<P>Posteriormente se agregar&aacute; el alumno al curso "AII_001 Derecho".</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_8.jpg"></P>

<P>Al consultar nuevamente el kardex, ya se podr&aacute; ver la asignatura que 
  fue agregada durante el proceso de inscripci&oacute;n.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_9.jpg"></P>

<H4>b) Utilización de Cursos en Secundaria</H4>

<P>A nivel Secundaria solo se utilizan cursos para cubrir necesidades muy espec&iacute;ficas, 
  por ejemplo: los talleres de educaci&oacute;n art&iacute;stica o t&eacute;cnica, 
  y algunas clases especiales. </P>
<P>Las instituciones pueden formar cursos especiales con distintas opciones de 
  cursado, por ejemplo distintos talleres, d&iacute;as y horarios. Para controlar 
  esta situación se recomienda crear distintos cursos con la variante del horario.</P>
<P>En nuestro ejemplo, tenemos un curso por cada taller y est&aacute;n relacionados 
  con la asignatura Artes I o Tecnología I. En la siguiente ventana se aprecian 
  los cursos de MUSICA y ARTES PLASTICAS que están relacionados con Artes y otros 
  de COMPUTACION y DIBUJO TECNICO relacionados a Tecnología.</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_10.jpg"></P>

<P>Esto nos dará la posibilidad de contar con subgrupos de alumnos de acuerdo 
  al taller elegido, tal como acontece en las instituciones de educaci&oacute;n 
  secundaria.</P>

<P>El taller de M&uacute;sica (1_MUSICA) puede tener inscritos alumnos de diferentes 
  Grupos (p.e. 1A, 1B, etc.).</P>

<P align=center><IMG src="<?php echo $url;?>cursos2_12.jpg"></P>

